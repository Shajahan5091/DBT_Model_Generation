with customer_base as (
    select
        customer_id,
        first_name,
        last_name,
        email,
        inserted_at
    from {{ source('dwh_raw', 'customers') }}
),

customer_orders as (
    select
        customer_id,
        sum(order_amount) as total_order_amount_365d
    from {{ source('dwh_raw', 'orders') }}
    where order_date >= current_date - 365
    group by customer_id
),

final as (
    select
        -- Direct mapping
        cb.customer_id,
        
        -- Trim and uppercase first name
        upper(trim(cb.first_name)) as first_name,
        
        -- Trim and uppercase last name  
        upper(trim(cb.last_name)) as last_name,
        
        -- Lowercase email with basic pattern validation
        case 
            when cb.email is not null 
                and cb.email like '%@%.%' 
                and length(cb.email) > 5
            then lower(trim(cb.email))
            else null
        end as email,
        
        -- Convert timestamp to date with null handling
        coalesce(cb.inserted_at::date, current_date) as customer_since_date,
        
        -- Derive customer tier based on spending in last 365 days
        case 
            when coalesce(co.total_order_amount_365d, 0) >= 1000 then 'Gold'
            when coalesce(co.total_order_amount_365d, 0) >= 500 then 'Silver'
            else 'Bronze'
        end as customer_tier
        
    from customer_base cb
    left join customer_orders co
        on cb.customer_id = co.customer_id
)

select * from final