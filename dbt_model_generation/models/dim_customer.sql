{{ config(
    materialized = 'table',
    schema = 'dwh_ma',
    alias = 'dim_customer'
) }}

WITH customers_source AS (
    SELECT
        customer_id,
        first_name,
        last_name,
        email,
        inserted_at
    FROM {{ source('dwh_raw', 'customers') }}
),

orders_source AS (
    SELECT
        customer_id,
        order_id,
        total_amount,
        order_date
    FROM {{ source('dwh_raw', 'orders') }}
),

customer_spending AS (
    SELECT
        customer_id,
        SUM(total_amount) AS total_spending_365_days
    FROM orders_source
    WHERE order_date >= DATEADD('day', -365, CURRENT_DATE())
    GROUP BY customer_id
),

transformed AS (
    SELECT
        -- Direct mapping
        c.customer_id,
        
        -- Trim and uppercase transformations
        UPPER(TRIM(c.first_name)) AS first_name,
        UPPER(TRIM(c.last_name)) AS last_name,
        
        -- Email validation and lowercase transformation
        CASE 
            WHEN c.email IS NOT NULL 
                AND c.email LIKE '%@%.%' 
                AND LENGTH(c.email) > 5
            THEN LOWER(c.email)
            ELSE NULL
        END AS email,
        
        -- Convert timestamp to date with null handling
        COALESCE(DATE(c.inserted_at), CURRENT_DATE()) AS customer_since_date,
        
        -- Derive customer tier based on spending
        CASE 
            WHEN COALESCE(cs.total_spending_365_days, 0) >= 1000 THEN 'Gold'
            WHEN COALESCE(cs.total_spending_365_days, 0) >= 500 THEN 'Silver'
            ELSE 'Bronze'
        END AS customer_tier
        
    FROM customers_source c
    LEFT JOIN customer_spending cs ON c.customer_id = cs.customer_id
)

SELECT * FROM transformed