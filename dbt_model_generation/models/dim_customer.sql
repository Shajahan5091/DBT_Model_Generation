{{ config(materialized='table') }}

with source_customers as (
    select 
        customer_id,
        first_name,
        last_name,
        email,
        inserted_at
    from {{ source('dwh_raw', 'customers') }}
),

source_orders as (
    select 
        customer_id,
        order_id,
        total_amount,
        order_date
    from {{ source('dwh_raw', 'orders') }}
    where order_date >= current_date - 365
),

customer_spending as (
    select 
        customer_id,
        sum(total_amount) as total_spending_365_days
    from source_orders
    group by customer_id
),

transformed_customers as (
    select 
        -- Direct mapping with proper alias
        sc.customer_id,
        
        -- Trim and uppercase first name
        upper(trim(sc.first_name)) as first_name,
        
        -- Trim and uppercase last name  
        upper(trim(sc.last_name)) as last_name,
        
        -- Lowercase email with validation
        case 
            when sc.email is null or sc.email = '' then null
            when sc.email rlike '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$' then lower(trim(sc.email))
            else null
        end as email,
        
        -- Convert timestamp to date with default handling
        coalesce(sc.inserted_at::date, current_date) as customer_since_date,
        
        -- Derive customer tier based on spending
        case 
            when coalesce(cs.total_spending_365_days, 0) >= 1000 then 'Gold'
            when coalesce(cs.total_spending_365_days, 0) >= 500 then 'Silver'
            else 'Bronze'
        end as customer_tier,
        
        -- Data quality flag for email issues
        case 
            when sc.email is not null and sc.email != '' and not (sc.email rlike '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$') then 'Invalid email format'
            else null
        end as data_quality_issue
        
    from source_customers sc
    left join customer_spending cs on sc.customer_id = cs.customer_id
)

select * from transformed_customers