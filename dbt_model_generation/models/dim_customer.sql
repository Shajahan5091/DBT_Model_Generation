{{ config(materialized='table') }}

with source_customers as (
    select 
        customer_id,
        first_name,
        last_name,
        email,
        inserted_at
    from {{ source('dwh_raw', 'customers') }}
),

source_orders as (
    select 
        customer_id,
        order_id,
        total_amount,
        order_date
    from {{ source('dwh_raw', 'orders') }}
    where order_date >= current_date - 365
),

customer_spending as (
    select 
        customer_id,
        sum(total_amount) as total_spending_365_days
    from source_orders
    group by customer_id
),

transformed_customers as (
    select 
        c.customer_id,
        
        -- Transform first_name: trim and uppercase
        upper(trim(c.first_name)) as first_name,
        
        -- Transform last_name: trim and uppercase  
        upper(trim(c.last_name)) as last_name,
        
        -- Transform email: lowercase with pattern validation
        case 
            when c.email is not null 
                and c.email like '%@%.%' 
                and length(c.email) > 5
            then lower(trim(c.email))
            else null
        end as email,
        
        -- Convert timestamp to date, default to current_date if null
        coalesce(c.inserted_at::date, current_date) as customer_since_date,
        
        -- Derive customer tier based on spending in last 365 days
        case 
            when coalesce(cs.total_spending_365_days, 0) >= 1000 then 'Gold'
            when coalesce(cs.total_spending_365_days, 0) >= 500 then 'Silver'
            else 'Bronze'
        end as customer_tier
        
    from source_customers c
    left join customer_spending cs 
        on c.customer_id = cs.customer_id
)

select * from transformed_customers