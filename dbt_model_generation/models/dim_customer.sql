{{ config(materialized='table') }}

with source_customers as (
    select 
        customer_id,
        first_name,
        last_name,
        email,
        inserted_at
    from {{ source('dwh_raw', 'customers') }}
),

source_orders as (
    select 
        customer_id,
        order_id,
        order_amount,
        order_date
    from {{ source('dwh_raw', 'orders') }}
),

customer_order_totals as (
    select 
        customer_id,
        sum(order_amount) as total_order_amount_365_days
    from source_orders
    where order_date >= dateadd('day', -365, current_date())
    group by customer_id
),

transformed_customers as (
    select 
        c.customer_id,
        
        -- Transform first_name: trim and uppercase
        upper(trim(c.first_name)) as first_name,
        
        -- Transform last_name: trim and uppercase  
        upper(trim(c.last_name)) as last_name,
        
        -- Transform email: lowercase with validation
        case 
            when c.email is not null 
                and c.email like '%@%.%' 
                and length(c.email) > 5
            then lower(trim(c.email))
            else null
        end as email,
        
        -- Convert timestamp to date, default to current_date if null
        coalesce(c.inserted_at::date, current_date()) as customer_since_date,
        
        -- Derive customer tier based on order history
        case 
            when coalesce(cot.total_order_amount_365_days, 0) >= 1000 then 'Gold'
            when coalesce(cot.total_order_amount_365_days, 0) >= 500 then 'Silver'
            else 'Bronze'
        end as customer_tier,
        
        -- Data quality flag for email issues
        case 
            when c.email is not null 
                and (c.email not like '%@%.%' or length(c.email) <= 5)
            then 'Email failed pattern validation'
            else null
        end as data_quality_issue
        
    from source_customers c
    left join customer_order_totals cot
        on c.customer_id = cot.customer_id
)

select * from transformed_customers