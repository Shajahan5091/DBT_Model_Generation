WITH source_data AS (
    SELECT
        src.address_id,
        src.customer_id,
        src.address_line1,
        src.city,
        src.country,
        src.inserted_at
    FROM {{ source('dwh_raw', 'addresses') }} src
),

customer_lookup AS (
    SELECT
        customer_id
    FROM {{ ref('dim_customer') }}
),

transformed AS (
    SELECT
        src.address_id,
        src.customer_id,
        TRIM(src.address_line1) AS address_line1,
        UPPER(src.city) AS city,
        src.country,
        DATE(src.inserted_at) AS inserted_at
    FROM source_data src
    INNER JOIN customer_lookup cust
        ON src.customer_id = cust.customer_id
)

SELECT * FROM transformed