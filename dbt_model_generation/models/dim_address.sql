{{ config(
    materialized = 'table',
    schema = 'dwh_ma',
    alias = 'dim_address'
) }}

WITH source_data AS (
    SELECT
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    FROM {{ source('dwh_raw', 'addresses') }}
),

customer_lookup AS (
    SELECT DISTINCT customer_id
    FROM {{ ref('dim_customer') }}
),

validated_data AS (
    SELECT
        s.address_id,
        s.customer_id,
        s.address_line1,
        s.city,
        s.country,
        s.inserted_at
    FROM source_data s
    INNER JOIN customer_lookup c
        ON s.customer_id = c.customer_id
),

transformed AS (
    SELECT
        address_id,
        customer_id,
        TRIM(address_line1) AS address_line1,
        UPPER(city) AS city,
        country,
        DATE(inserted_at) AS inserted_at
    FROM validated_data
)

SELECT * FROM transformed