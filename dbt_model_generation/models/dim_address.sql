WITH source_data AS (
    SELECT
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    FROM {{ source('dwh_raw', 'addresses') }}
),

customer_lookup AS (
    SELECT
        customer_id
    FROM {{ ref('dim_customer') }}
),

validated_data AS (
    SELECT
        s.address_id,
        s.customer_id,
        s.address_line1,
        s.city,
        s.country,
        s.inserted_at
    FROM source_data s
    INNER JOIN customer_lookup c
        ON s.customer_id = c.customer_id
),

transformed AS (
    SELECT
        address_id AS addr_address_id,
        customer_id AS addr_customer_id,
        TRIM(address_line1) AS addr_address_line1,
        UPPER(city) AS addr_city,
        country AS addr_country,
        DATE(inserted_at) AS addr_inserted_at
    FROM validated_data
)

SELECT * FROM transformed