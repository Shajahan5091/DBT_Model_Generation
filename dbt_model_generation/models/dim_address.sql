{{ config(
    materialized='table'
) }}

with source_addresses as (
    select
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    from {{ source('dwh_raw', 'addresses') }}
),

customer_lookup as (
    select
        customer_id
    from {{ ref('dim_customer') }}
),

validated_addresses as (
    select
        sa.address_id,
        sa.customer_id,
        sa.address_line1,
        sa.city,
        sa.country,
        sa.inserted_at
    from source_addresses sa
    inner join customer_lookup cl
        on sa.customer_id = cl.customer_id
),

final as (
    select
        address_id,
        customer_id,
        trim(address_line1) as address_line1,
        upper(city) as city,
        country,
        date(inserted_at) as inserted_at
    from validated_addresses
)

select * from final