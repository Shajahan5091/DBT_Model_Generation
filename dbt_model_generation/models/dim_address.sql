WITH source_data AS (
    SELECT
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    FROM {{ source('dwh_raw', 'addresses') }}
),

customer_lookup AS (
    SELECT
        customer_id
    FROM {{ ref('dim_customer') }}
),

transformed AS (
    SELECT
        src.address_id as address_id,
        src.customer_id as customer_id,
        TRIM(src.address_line1) as address_line1,
        UPPER(src.city) as city,
        src.country as country,
        DATE(src.inserted_at) as inserted_at
    FROM source_data src
    INNER JOIN customer_lookup cl
        ON src.customer_id = cl.customer_id
)

SELECT * FROM transformed