{{ config(
    materialized='table'
) }}

with source_data as (
    select
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    from {{ source('dwh_raw', 'addresses') }}
),

customer_lookup as (
    select
        customer_id
    from {{ ref('dim_customer') }}
),

validated_data as (
    select
        s.address_id,
        s.customer_id,
        s.address_line1,
        s.city,
        s.country,
        s.inserted_at
    from source_data s
    inner join customer_lookup c
        on s.customer_id = c.customer_id
),

final as (
    select
        address_id,
        customer_id,
        trim(address_line1) as address_line1,
        upper(city) as city,
        country,
        date(inserted_at) as inserted_at
    from validated_data
)

select * from final