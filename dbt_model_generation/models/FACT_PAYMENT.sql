TARGET TABLE: bsl_ma.dwh_ma.fact_payment
SOURCE TABLES: bsl_raw.dwh_raw.payments, bsl_ma.dwh_ma.fact_order
DESCRIPTION: This model transforms raw payment data into a structured fact table with proper data types, validation, and business logic applied. It includes payment method standardization and payment application status calculation.
PREREQUISITES: fact_order model must exist and be populated
