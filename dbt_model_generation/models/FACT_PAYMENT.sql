MODEL NAME: fact_payment
TARGET TABLE: bsl_ma.dwh_ma.fact_payment
SOURCE TABLES: bsl_raw.dwh_raw.payments, bsl_ma.dwh_ma.fact_order
DESCRIPTION: This model transforms raw payment data into a fact table with proper data types, validation, and business logic. It includes payment method standardization, order lookup validation, and payment application status calculation.
PREREQUISITES: fact_order model must exist and be populated
PARAMETER: Incremental model based on payment_date
AUTHOUR: AI Generated by Shajahan - 2024-12-19
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='payment_id'
) }}

with source_payments as (
    select *
    from {{ source('dwh_raw', 'payments') }}
    {% if is_incremental() %}
        where payment_date::date > (select coalesce(max(payment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

fact_order as (
    select 
        order_id,
        total_amount
    from {{ ref('fact_order') }}
),

payment_with_order as (
    select 
        sp.payment_id,
        sp.order_id,
        sp.payment_date,
        sp.payment_method,
        sp.amount,
        fo.order_id as lookup_order_id,
        fo.total_amount as order_total_amount
    from source_payments sp
    left join fact_order fo on sp.order_id = fo.order_id::varchar
    -- Skip payments if order not present
    where fo.order_id is not null
),

payment_aggregates as (
    select 
        order_id,
        sum(case when amount >= 0 then amount else 0 end) as total_payments
    from payment_with_order
    group by order_id
),

final as (
    select 
        -- Direct mapping of payment_id
        pwo.payment_id,
        
        -- Convert order_id to integer via lookup
        pwo.lookup_order_id::integer as order_id,
        
        -- Convert timestamp to date
        pwo.payment_date::date as payment_date,
        
        -- Standardize payment method to enum values
        case 
            when upper(pwo.payment_method) in ('CARD', 'UPI', 'COD', 'NETBANKING') 
            then upper(pwo.payment_method)
            else 'CARD'
        end as payment_method,
        
        -- Handle negative amounts by setting to null
        case 
            when pwo.amount < 0 then null
            else pwo.amount
        end as amount,
        
        -- Calculate if payment is fully applied
        case 
            when pwo.order_total_amount is null then false
            when pa.total_payments >= pwo.order_total_amount then true
            else false
        end as is_fully_applied
        
    from payment_with_order pwo
    left join payment_aggregates pa on pwo.order_id = pa.order_id
)

select * from final