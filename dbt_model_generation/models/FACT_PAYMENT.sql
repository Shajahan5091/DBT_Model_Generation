MODEL NAME: FACT_PAYMENT
TARGET TABLE: BSL_MA.DWH_MA.FACT_PAYMENT
SOURCE TABLES: BSL_RAW.DWH_RAW.PAYMENT, BSL_MA.DWH_MA.FACT_ORDER
DESCRIPTION: Payment fact table at payment event level with surrogate keys and business logic transformations
PREREQUISITES: FACT_ORDER table must exist for order lookups
PARAMETER: Incremental model based on PAYMENT_DATE
AUTHOR: AI Generated by Shajahan
CREATED DATE: 2024-12-19 (IST)
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='PAYMENT_ID'
) }}

with source_payments as (
    select *
    from {{ source('dwh_raw', 'payment') }}
    {% if is_incremental() %}
    where PAYMENT_DATE::date > (select coalesce(max(PAYMENT_DATE), '1900-01-01') from {{ this }})
    {% endif %}
),

fact_order as (
    select 
        ORDER_ID,
        ORDER_SK,
        TOTAL_AMOUNT
    from {{ ref('FACT_ORDER') }}
),

-- Calculate payment totals per order for fully applied flag
payment_totals as (
    select 
        ORDER_ID,
        sum(case when AMOUNT >= 0 then AMOUNT else 0 end) as total_payments
    from source_payments
    group by ORDER_ID
),

transformed as (
    select
        -- Generate surrogate key from PAYMENT_ID
        {{ dbt_utils.generate_surrogate_key(['p.PAYMENT_ID']) }} as PAYMENT_SK,
        
        -- Direct mapping of PAYMENT_ID
        p.PAYMENT_ID,
        
        -- Lookup ORDER_SK from FACT_ORDER
        fo.ORDER_SK,
        
        -- Convert timestamp to date
        p.PAYMENT_DATE::date as PAYMENT_DATE,
        
        -- Uppercase and map payment method to enum
        case 
            when upper(p.PAYMENT_METHOD) in ('CARD', 'UPI', 'COD', 'NETBANKING') 
            then upper(p.PAYMENT_METHOD)
            else 'CARD'
        end as PAYMENT_METHOD,
        
        -- Handle negative amounts
        case 
            when p.AMOUNT < 0 then null
            else p.AMOUNT::number(10,2)
        end as AMOUNT,
        
        -- Calculate IS_FULLY_APPLIED flag
        case 
            when fo.TOTAL_AMOUNT is null then false
            when pt.total_payments >= fo.TOTAL_AMOUNT then true
            else false
        end as IS_FULLY_APPLIED
        
    from source_payments p
    inner join fact_order fo on p.ORDER_ID = fo.ORDER_ID  -- Skip payment if order not present
    left join payment_totals pt on p.ORDER_ID = pt.ORDER_ID
)

select * from transformed