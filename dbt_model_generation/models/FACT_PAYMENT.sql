{{ config(
    materialized='incremental',
    unique_key='payment_id'
) }}

with source_payments as (
    select * from {{ source('dwh_raw', 'payments') }}
    {% if is_incremental() %}
    where payment_date::date > (select coalesce(max(payment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

fact_order as (
    select 
        order_id,
        total_amount
    from {{ ref('fact_order') }}
),

payment_aggregates as (
    select 
        order_id,
        sum(case when amount > 0 then amount else 0 end) as total_payments
    from source_payments sp
    group by order_id
),

transformed as (
    select
        sp.payment_id,
        fo.order_id::integer as order_id,
        sp.payment_date::date as payment_date,
        case 
            when upper(sp.payment_method) in ('CARD', 'UPI', 'COD', 'NETBANKING') 
            then upper(sp.payment_method)
            else 'CARD'
        end as payment_method,
        case 
            when sp.amount < 0 then null
            else sp.amount::number(10,2)
        end as amount,
        case 
            when fo.total_amount is null then false
            when pa.total_payments >= fo.total_amount then true
            else false
        end as is_fully_applied
    from source_payments sp
    inner join fact_order fo on sp.order_id = fo.order_id
    left join payment_aggregates pa on fo.order_id = pa.order_id
)

select * from transformed