{{ config(
    materialized='incremental',
    unique_key='payment_id',
    on_schema_change='fail'
) }}

with source_payments as (
    select 
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount
    from {{ source('dwh_raw', 'payments') }}
    {% if is_incremental() %}
        where payment_date::date > (select coalesce(max(payment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

fact_order_lookup as (
    select 
        order_id,
        total_amount
    from {{ ref('fact_order') }}
),

payment_transformations as (
    select 
        sp.payment_id,
        -- Lookup order_id and convert to integer, skip payment if order not present
        fo.order_id::integer as order_id,
        -- Convert timestamp to date
        sp.payment_date::date as payment_date,
        -- Uppercase method and map to enum, default to 'CARD' if not in list
        case 
            when upper(sp.payment_method) in ('CARD', 'UPI', 'COD', 'NETBANKING') 
            then upper(sp.payment_method)
            else 'CARD'
        end as payment_method,
        -- Direct mapping, set to null if negative
        case 
            when sp.amount < 0 then null
            else sp.amount
        end as amount,
        fo.total_amount
    from source_payments sp
    inner join fact_order_lookup fo on sp.order_id = fo.order_id
),

payment_aggregations as (
    select 
        order_id,
        sum(coalesce(amount, 0)) as total_payments
    from payment_transformations
    group by order_id
),

final as (
    select 
        pt.payment_id,
        pt.order_id,
        pt.payment_date,
        pt.payment_method,
        pt.amount,
        -- Flag true if sum(payments) >= order total_amount, false if order total is null
        case 
            when pt.total_amount is null then false
            when pa.total_payments >= pt.total_amount then true
            else false
        end as is_fully_applied
    from payment_transformations pt
    left join payment_aggregations pa on pt.order_id = pa.order_id
)

select * from final