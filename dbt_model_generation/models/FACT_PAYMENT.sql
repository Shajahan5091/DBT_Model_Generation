MODEL NAME: fact_payment
TARGET TABLE: bsl_ma.dwh_ma.fact_payment
SOURCE TABLES: bsl_raw.dwh_raw.payments
DESCRIPTION: This model transforms raw payment data into a cleaned and enriched fact table for analytics. It performs data type conversions, validates payment methods, handles negative amounts, and calculates payment application status by joining with order data.
PREREQUISITES: fact_order model must exist and be populated with order data
PARAMETER: None
