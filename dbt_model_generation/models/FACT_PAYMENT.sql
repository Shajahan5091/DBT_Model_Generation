{{ config(
    materialized='incremental',
    unique_key='PAYMENT_ID'
) }}

WITH payment_source AS (
    SELECT 
        PAYMENT_ID,
        ORDER_ID,
        PAYMENT_DATE,
        PAYMENT_METHOD,
        AMOUNT
    FROM {{ source('BSL_RAW', 'PAYMENT') }}
    {% if is_incremental() %}
        WHERE PAYMENT_DATE::date > (SELECT COALESCE(MAX(PAYMENT_DATE), '1900-01-01') FROM {{ this }})
    {% endif %}
),

order_lookup AS (
    SELECT 
        ORDER_ID,
        ORDER_SK,
        TOTAL_AMOUNT
    FROM {{ ref('FACT_ORDER') }}
),

payment_aggregated AS (
    SELECT 
        ORDER_ID,
        SUM(CASE WHEN AMOUNT < 0 THEN NULL ELSE AMOUNT END) AS total_payments
    FROM payment_source
    GROUP BY ORDER_ID
),

transformed_payments AS (
    SELECT 
        -- Generate surrogate key from PAYMENT_ID
        {{ dbt_utils.generate_surrogate_key(['p.PAYMENT_ID']) }} AS PAYMENT_SK,
        
        -- Direct mapping of PAYMENT_ID
        p.PAYMENT_ID,
        
        -- Lookup ORDER_SK from FACT_ORDER
        o.ORDER_SK,
        
        -- Convert timestamp to date
        p.PAYMENT_DATE::DATE AS PAYMENT_DATE,
        
        -- Uppercase and map payment method to enum
        CASE 
            WHEN UPPER(p.PAYMENT_METHOD) IN ('CARD', 'UPI', 'COD', 'NETBANKING') 
            THEN UPPER(p.PAYMENT_METHOD)
            ELSE 'CARD'
        END AS PAYMENT_METHOD,
        
        -- Handle negative amounts
        CASE 
            WHEN p.AMOUNT < 0 THEN NULL
            ELSE p.AMOUNT::NUMBER(10,2)
        END AS AMOUNT,
        
        -- Calculate IS_FULLY_APPLIED flag
        CASE 
            WHEN o.TOTAL_AMOUNT IS NULL THEN FALSE
            WHEN pa.total_payments >= o.TOTAL_AMOUNT THEN TRUE
            ELSE FALSE
        END AS IS_FULLY_APPLIED
        
    FROM payment_source p
    INNER JOIN order_lookup o ON p.ORDER_ID = o.ORDER_ID  -- Skip payment if order not present
    LEFT JOIN payment_aggregated pa ON p.ORDER_ID = pa.ORDER_ID
)

SELECT * FROM transformed_payments