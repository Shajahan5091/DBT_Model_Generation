MODEL NAME: dim_address
TARGET TABLE: bsl_ma.dwh_ma.dim_address
SOURCE TABLES: bsl_raw.dwh_raw.addresses
DESCRIPTION: Customer address dimension table that transforms raw address data into a structured dimensional model with surrogate keys and standardized formatting
PREREQUISITES: dim_customer table must exist for customer_sk lookup
PARAMETER: None
AUTHOUR: AI Generated by Shajahan - 2024-12-19 IST
------------------------------------------------------------------------
#}

{{ config(
    materialized='table'
) }}

with source_data as (
    select * from {{ source('dwh_raw', 'addresses') }}
),

customer_lookup as (
    select 
        customer_id,
        customer_sk
    from {{ ref('dim_customer') }}
),

transformed as (
    select
        -- Generate surrogate key from address_id
        {{ dbt_utils.generate_surrogate_key(['address_id']) }} as address_sk,
        
        -- Direct mapping of address_id
        s.address_id,
        
        -- Lookup customer_sk from dim_customer
        c.customer_sk,
        
        -- Trim whitespace from address_line1
        trim(s.address_line1) as address_line1,
        
        -- Uppercase city and normalize
        upper(s.city) as city,
        
        -- Direct mapping of country
        s.country,
        
        -- Convert timestamp to date
        s.inserted_at::date as inserted_at
        
    from source_data s
    inner join customer_lookup c
        on s.customer_id = c.customer_id
    -- Skip rows where customer_id not found in dim_customer
)

select * from transformed