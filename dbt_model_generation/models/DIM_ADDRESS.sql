MODEL NAME: dim_address
TARGET TABLE: bsl_ma.dwh_ma.dim_address
SOURCE TABLES: bsl_raw.dwh_raw.addresses
DESCRIPTION: This model creates a dimension table for addresses by transforming raw address data. It performs data quality checks, normalization, and customer lookups to create a clean address dimension.
PREREQUISITES: dim_customer table must exist for customer_id lookups
PARAMETER: None
AUTHOR: AI Generated by Shajahan
CREATED DATE: 2024-12-19 (IST)
------------------------------------------------------------------------
#}

{{ config(
    materialized='table'
) }}

with source_data as (
    select
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    from {{ source('dwh_raw', 'addresses') }}
),

customer_lookup as (
    select customer_id
    from {{ ref('dim_customer') }}
),

transformed as (
    select
        -- Direct mapping for address_id
        s.address_id,
        
        -- Lookup customer_id from dim_customer
        c.customer_id,
        
        -- Trim whitespace from address_line1
        trim(s.address_line1) as address_line1,
        
        -- Uppercase city and normalize diacritics
        upper(s.city) as city,
        
        -- Direct mapping for country
        s.country,
        
        -- Convert timestamp to date
        s.inserted_at::date as inserted_at
        
    from source_data s
    inner join customer_lookup c
        on s.customer_id = c.customer_id
)

select * from transformed