MODEL NAME: fact_shipment
TARGET TABLE: bsl_ma.dwh_ma.fact_shipment
SOURCE TABLES: bsl_raw.dwh_raw.shipments
DESCRIPTION: This model transforms raw shipment data into a fact table for analytics. It includes shipment details, order lookups, and calculated metrics like days to ship.
PREREQUISITES: fact_order table must exist for order_id lookups
PARAMETER: None
