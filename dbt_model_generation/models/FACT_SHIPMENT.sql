{{ config(
    materialized='incremental',
    unique_key='SHIPMENT_ID'
) }}

WITH shipment_data AS (
    SELECT 
        -- Generate surrogate key from SHIPMENT_ID
        {{ dbt_utils.generate_surrogate_key(['SHIPMENT_ID']) }} AS SHIPMENT_SK,
        
        -- Direct mapping of SHIPMENT_ID
        SHIPMENT_ID,
        
        -- Convert timestamp to date
        SHIPPED_DATE::DATE AS SHIPPED_DATE,
        
        -- Uppercase carrier
        UPPER(CARRIER) AS CARRIER,
        
        -- Direct mapping of tracking number
        TRACKING_NUMBER,
        
        -- Keep original fields for joins and calculations
        ORDER_ID,
        ORDER_DATE
        
    FROM {{ source('BSL_RAW', 'SHIPMENT') }}
    
    {% if is_incremental() %}
        WHERE SHIPPED_DATE::date > (select coalesce(max(SHIPPED_DATE), '1900-01-01') from {{ this }})
    {% endif %}
),

final AS (
    SELECT 
        s.SHIPMENT_SK,
        s.SHIPMENT_ID,
        
        -- Lookup ORDER_SK from FACT_ORDER using ORDER_ID
        o.ORDER_SK,
        
        s.SHIPPED_DATE,
        
        -- Calculate days to ship with floor at 0 for negative values
        GREATEST(0, DATEDIFF('day', o.ORDER_DATE, s.SHIPPED_DATE)) AS DAYS_TO_SHIP,
        
        s.CARRIER,
        s.TRACKING_NUMBER
        
    FROM shipment_data s
    -- Join with FACT_ORDER to get ORDER_SK and ORDER_DATE, skip if order missing
    INNER JOIN {{ ref('FACT_ORDER') }} o
        ON s.ORDER_ID = o.ORDER_ID
)

SELECT * FROM final