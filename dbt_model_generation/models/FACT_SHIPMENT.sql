{{ config(
    materialized='incremental',
    unique_key='shipment_id'
) }}

with source_data as (
    select 
        shipment_id,
        order_id as source_order_id,
        shipment_date,
        order_date,
        carrier,
        tracking_number
    from {{ source('dwh_raw', 'shipments') }}
    {% if is_incremental() %}
        where shipment_date::date > (select coalesce(max(shipment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

order_lookup as (
    select 
        order_id,
        order_date as fact_order_date
    from {{ ref('fact_order') }}
),

final as (
    select 
        s.shipment_id,
        o.order_id,
        s.shipment_date::date as shipment_date,
        greatest(0, datediff('day', coalesce(o.fact_order_date, s.order_date), s.shipment_date)) as days_to_ship,
        upper(s.carrier) as carrier,
        s.tracking_number
    from source_data s
    left join order_lookup o 
        on s.source_order_id = o.order_id::varchar
    where o.order_id is not null  -- Skip if order missing
)

select * from final