{{ config(
    materialized='incremental',
    unique_key='shipment_id'
) }}

with source_shipments as (
    select 
        shipment_id,
        order_id,
        shipment_date,
        carrier,
        tracking_number
    from {{ source('dwh_raw', 'shipments') }}
    {% if is_incremental() %}
        where shipment_date::date > (select coalesce(max(shipment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

fact_order_lookup as (
    select 
        order_id,
        order_date
    from {{ ref('fact_order') }}
),

transformed_shipments as (
    select 
        s.shipment_id,
        fo.order_id::integer as order_id,
        s.shipment_date::date as shipment_date,
        greatest(0, datediff('day', fo.order_date, s.shipment_date)) as days_to_ship,
        upper(s.carrier) as carrier,
        s.tracking_number
    from source_shipments s
    inner join fact_order_lookup fo 
        on s.order_id = fo.order_id
)

select * from transformed_shipments