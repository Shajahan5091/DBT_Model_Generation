WITH source_data AS (
    SELECT 
        product_id,
        product_name,
        price
    FROM {{ source('dwh_raw', 'products') }}
),

transformed_data AS (
    SELECT 
        product_id,
        -- Transform product name to title case and trim excessive spaces
        INITCAP(REGEXP_REPLACE(TRIM(product_name), '\\s+', ' ')) AS product_name,
        -- Direct mapping for price
        price::NUMBER(10,2) AS price,
        -- Create price band based on price ranges
        CASE 
            WHEN price < 50 THEN 'Low'
            WHEN price >= 50 AND price <= 199.99 THEN 'Mid'
            WHEN price >= 200 AND price <= 499.99 THEN 'High'
            WHEN price >= 500 THEN 'Premium'
            ELSE 'Unknown'
        END AS price_band,
        -- Add notes for zero price items
        CASE 
            WHEN price = 0 THEN 'Price is zero - flagged for review'
            ELSE NULL
        END AS notes
    FROM source_data
)

SELECT 
    product_id,
    product_name,
    price,
    price_band,
    notes
FROM transformed_data