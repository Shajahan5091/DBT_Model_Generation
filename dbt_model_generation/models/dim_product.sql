SOURCE TABLES: BSL_RAW.DWH_RAW.PRODUCTS
DESCRIPTION: Dimension table for products with transformed product names, price bands, and data quality flags
PREREQUISITES: Source table BSL_RAW.DWH_RAW.PRODUCTS must be available
