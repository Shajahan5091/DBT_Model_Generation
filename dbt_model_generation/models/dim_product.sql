{{ config(
    materialized = 'table',
    alias = 'dim_product'
) }}

WITH source_data AS (
    SELECT
        product_id,
        product_name,
        price
    FROM {{ source('dwh_raw', 'products') }}
),

transformed AS (
    SELECT
        product_id,
        -- Title-case product name and trim excessive spaces
        INITCAP(TRIM(REGEXP_REPLACE(product_name, '\\s+', ' '))) AS product_name,
        -- Direct mapping for price
        price,
        -- Price banding logic
        CASE 
            WHEN price < 50 THEN 'Low'
            WHEN price >= 50 AND price <= 199.99 THEN 'Mid'
            WHEN price >= 200 AND price <= 499.99 THEN 'High'
            WHEN price >= 500 THEN 'Premium'
            ELSE 'Unknown'
        END AS price_band,
        -- Flag for review if price is 0
        CASE 
            WHEN price = 0 THEN 'Price requires review - zero value detected'
            ELSE NULL
        END AS review_notes
    FROM source_data
)

SELECT * FROM transformed