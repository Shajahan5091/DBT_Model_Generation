{{ config(
    materialized = 'table',
    alias = 'dim_product'
) }}

WITH source_data AS (
    SELECT
        product_id,
        product_name,
        price
    FROM {{ source('dwh_raw', 'products') }}
),

transformed AS (
    SELECT
        product_id AS dim_product_product_id,
        INITCAP(TRIM(REGEXP_REPLACE(product_name, '\\s+', ' '))) AS dim_product_product_name,
        price AS dim_product_price,
        CASE 
            WHEN price < 50 THEN 'Low'
            WHEN price >= 50 AND price <= 199.99 THEN 'Mid'
            WHEN price >= 200 AND price <= 499.99 THEN 'High'
            WHEN price >= 500 THEN 'Premium'
            ELSE 'Unknown'
        END AS dim_product_price_band,
        CASE 
            WHEN price = 0 THEN 'Price is zero - flagged for review'
            ELSE NULL
        END AS dim_product_notes
    FROM source_data
)

SELECT * FROM transformed