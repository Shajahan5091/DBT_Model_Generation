{{ config(
    materialized='table',
    pre_hook="DELETE FROM {{ this }} WHERE PRODUCT_ID IN (SELECT PRODUCT_ID FROM {{ source('raw', 'product') }} WHERE PRICE IS NOT NULL AND PRICE >= 0)"
