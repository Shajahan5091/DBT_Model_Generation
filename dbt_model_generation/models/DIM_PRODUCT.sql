SELECT 
    -- Direct mapping with unique constraint
    p.product_id,
    
    -- Title-case product name with trimmed spaces
    INITCAP(TRIM(REGEXP_REPLACE(p.product_name, '\\s+', ' '))) AS product_name,
    
    -- Category lookup with fallback to 0 for missing categories
    COALESCE(dc.category_id, 0) AS category_id,
    
    -- Direct price mapping with precision
    CAST(p.price AS NUMBER(10,2)) AS price,
    
    -- Price banding logic
    CASE 
        WHEN p.price < 50 THEN 'Low'
        WHEN p.price >= 50 AND p.price <= 199.99 THEN 'Mid'
        WHEN p.price >= 200 AND p.price <= 499.99 THEN 'High'
        WHEN p.price >= 500 THEN 'Premium'
        ELSE 'Unknown'
    END AS price_band,
    
    -- Flag for zero price review
    CASE 
        WHEN p.price = 0 THEN 'Price is zero - requires review'
        ELSE NULL
    END AS review_notes

FROM {{ source('dwh_raw', 'products') }} p
LEFT JOIN {{ ref('dim_category') }} dc 
    ON p.category_id = dc.category_id