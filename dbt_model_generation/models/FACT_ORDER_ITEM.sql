{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

with source_data as (
    select *
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
    where exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

order_lookup as (
    select order_id
    from {{ ref('fact_order') }}
),

product_lookup as (
    select 
        product_id,
        coalesce(product_sk, 0) as product_sk
    from {{ ref('dim_product') }}
),

transformed as (
    select
        -- Lookup order_id from fact_order
        fo.order_id::integer as order_id,
        
        -- Lookup product_id from dim_product, default to 0 if missing
        coalesce(dp.product_id::integer, 0) as product_id,
        
        -- Direct mapping with integer semantics
        round(s.quantity)::number as quantity,
        
        -- Direct mapping for unit_price
        s.unit_price::number(10,2) as unit_price,
        
        -- Compute extended_amount (quantity * unit_price)
        (round(s.quantity) * coalesce(s.unit_price, 0))::number(12,2) as extended_amount,
        
        -- Set discount_amount to 0 for now (can be enhanced with business rules later)
        0::number(12,2) as discount_amount,
        
        -- Compute net_amount (extended_amount - discount_amount), ensure non-negative
        greatest(
            (round(s.quantity) * coalesce(s.unit_price, 0)) - 0, 
            0
        )::number(12,2) as net_amount,
        
        -- Keep original order_item_id for reference
        s.order_item_id
        
    from source_data s
    inner join order_lookup fo on s.order_id = fo.order_id
    left join product_lookup dp on s.product_id = dp.product_id
    
    -- Skip items if order_id missing in fact_order (handled by inner join)
)

select * from transformed