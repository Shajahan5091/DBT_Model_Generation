{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

with source_data as (
    select 
        order_id,
        product_id,
        quantity,
        unit_price,
        order_item_id
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
        where exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

order_lookup as (
    select 
        order_id
    from {{ ref('fact_order') }}
),

product_lookup as (
    select 
        product_id,
        coalesce(product_sk, 0) as product_sk
    from {{ ref('dim_product') }}
),

transformed_data as (
    select 
        -- Order ID lookup with filtering
        cast(o.order_id as integer) as order_id,
        
        -- Product ID lookup with unknown handling
        cast(coalesce(p.product_id, s.product_id) as integer) as product_id,
        coalesce(p.product_sk, 0) as product_sk,
        
        -- Direct mappings with data type handling
        round(s.quantity) as quantity,
        cast(s.unit_price as number(10,2)) as unit_price,
        
        -- Calculated fields
        cast(round(s.quantity) * coalesce(s.unit_price, 0) as number(12,2)) as extended_amount,
        
        -- Discount allocation (set to 0 for now as no discount context available)
        cast(0 as number(12,2)) as discount_amount,
        
        s.order_item_id
        
    from source_data s
    inner join order_lookup o on s.order_id = o.order_id  -- Skip items if order_id missing
    left join product_lookup p on s.product_id = p.product_id  -- Left join to handle missing products
),

final_calculations as (
    select 
        order_id,
        product_id,
        product_sk,
        quantity,
        unit_price,
        extended_amount,
        discount_amount,
        -- Net amount calculation with non-negative constraint
        greatest(extended_amount - discount_amount, 0) as net_amount,
        order_item_id
    from transformed_data
)

select * from final_calculations