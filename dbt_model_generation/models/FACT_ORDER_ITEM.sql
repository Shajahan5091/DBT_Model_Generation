{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

with source_data as (
    select 
        order_id,
        product_id,
        quantity,
        unit_price,
        order_item_id
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
    where exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

order_lookup as (
    select 
        order_id as order_id_lookup
    from {{ ref('fact_order') }}
),

product_lookup as (
    select 
        product_id as product_id_lookup,
        coalesce(product_sk, 0) as product_sk
    from {{ ref('dim_product') }}
),

transformed_data as (
    select 
        -- Order ID lookup with filtering
        case 
            when ol.order_id_lookup is not null 
            then cast(sd.order_id as integer)
            else null
        end as order_id,
        
        -- Product ID lookup with unknown handling
        case 
            when pl.product_id_lookup is not null 
            then cast(sd.product_id as integer)
            else 0
        end as product_id,
        
        -- Quantity with integer semantics
        round(sd.quantity) as quantity,
        
        -- Unit price direct mapping
        cast(sd.unit_price as number(10,2)) as unit_price,
        
        -- Extended amount calculation
        cast(
            round(sd.quantity) * coalesce(cast(sd.unit_price as number(10,2)), 0)
            as number(12,2)
        ) as extended_amount,
        
        -- Discount amount (set to 0 for now as no discount context)
        cast(0 as number(12,2)) as discount_amount,
        
        -- Net amount calculation (floor at 0)
        cast(
            greatest(
                0,
                (round(sd.quantity) * coalesce(cast(sd.unit_price as number(10,2)), 0)) - 0
            ) as number(12,2)
        ) as net_amount,
        
        sd.order_item_id
        
    from source_data sd
    left join order_lookup ol on cast(sd.order_id as integer) = ol.order_id_lookup
    left join product_lookup pl on cast(sd.product_id as integer) = pl.product_id_lookup
    
    -- Skip items if order_id missing in fact_order
    where ol.order_id_lookup is not null
)

select * from transformed_data