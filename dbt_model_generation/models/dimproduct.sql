    select
        product_id,
        product_name,
        brand,
        category_name,
        unit_price,
        quantity_on_hand
    from {{ source('dwh_staging', 'product') }}
),

transformed_product as (
    select
        product_id,
        product_name,
        brand,
        category_name,
        -- Calculate current unit price considering discounts
