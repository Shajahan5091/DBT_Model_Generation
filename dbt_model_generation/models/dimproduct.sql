WITH product_base AS (
    SELECT 
        product_id,
        product_name,
        brand,
        product_category_id
    FROM {{ source('dwh_staging', 'product') }}
),

product_category_lookup AS (
    SELECT 
        product_category_id,
        category_name
    FROM {{ source('dwh_staging', 'product_category') }}
),

product_discount_lookup AS (
    SELECT 
        product_id,
        discount_pct,
        effective_from,
        effective_to
    FROM {{ source('dwh_staging', 'product_discount') }}
    WHERE CURRENT_DATE() BETWEEN effective_from AND effective_to
),

inventory_lookup AS (
    SELECT 
        product_id,
        quantity_on_hand
    FROM {{ source('dwh_staging', 'inventory') }}
),

final AS (
    SELECT 
        p.product_id,
        p.product_name,
        p.brand,
        pc.category_name,
        -- Calculate current unit price based on discount
        CASE 
            WHEN pd.discount_pct IS NOT NULL 
            THEN ROUND((100 - pd.discount_pct) / 100.0, 2)
            ELSE NULL 
        END AS current_unit_price,
        -- Determine active flag based on inventory quantity
        CASE 
            WHEN i.quantity_on_hand > 0 THEN TRUE
            ELSE FALSE 
        END AS active_flag
    FROM product_base p
    LEFT JOIN product_category_lookup pc 
        ON p.product_category_id = pc.product_category_id
    LEFT JOIN product_discount_lookup pd 
        ON p.product_id = pd.product_id
    LEFT JOIN inventory_lookup i 
        ON p.product_id = i.product_id
)

SELECT * FROM final