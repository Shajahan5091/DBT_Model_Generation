DESCRIPTION: Dimension table that stores product master details including product name, brand, category, and pricing attributes with current unit price calculated based on discount and active flag determined by inventory levels.
PREREQUISITES: Source tables Product, Product_Category, Product_Discount, and Inventory must be available in staging layer
