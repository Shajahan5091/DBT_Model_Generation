{{ config(
    materialized = 'incremental',
    unique_key = 'order_item_id'
) }}

WITH source_order_items AS (
    SELECT
        oi.order_item_id,
        oi.order_id,
        oi.product_id,
        oi.quantity,
        oi.unit_price
    FROM {{ source('dwh_raw', 'order_items') }} oi
    {% if is_incremental() %}
    WHERE exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

source_orders AS (
    SELECT
        o.order_id
    FROM {{ source('dwh_raw', 'orders') }} o
),

order_lookup AS (
    SELECT
        oi.order_item_id,
        oi.order_id,
        oi.product_id,
        oi.quantity,
        oi.unit_price
    FROM source_order_items oi
    INNER JOIN {{ ref('fact_order') }} fo ON oi.order_id = fo.order_id
),

product_lookup AS (
    SELECT
        ol.order_item_id,
        ol.order_id,
        COALESCE(ol.product_id, '0') as product_id,
        ol.quantity,
        ol.unit_price
    FROM order_lookup ol
    LEFT JOIN {{ ref('dim_product') }} dp ON ol.product_id = dp.product_id
),

transformed AS (
    SELECT
        pl.order_item_id,
        pl.order_id,
        pl.product_id,
        ROUND(pl.quantity) as quantity,
        pl.unit_price,
        (ROUND(pl.quantity) * COALESCE(pl.unit_price, 0))::NUMBER(12,2) as extended_amount,
        0::NUMBER(12,2) as discount_amount,
        GREATEST(0, (ROUND(pl.quantity) * COALESCE(pl.unit_price, 0)) - 0)::NUMBER(12,2) as net_amount
    FROM product_lookup pl
)

SELECT * FROM transformed