{{ config(
    materialized = 'incremental',
    alias = 'fact_order_item'
) }}

WITH source_order_items AS (
    SELECT
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price
    FROM {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
    WHERE exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

source_orders AS (
    SELECT
        order_id
    FROM {{ source('dwh_raw', 'orders') }}
),

-- Validate order_id exists in fact_order
order_validation AS (
    SELECT
        oi.order_item_id,
        oi.order_id,
        oi.product_id,
        oi.quantity,
        oi.unit_price
    FROM source_order_items oi
    INNER JOIN {{ ref('fact_order') }} fo ON oi.order_id = fo.order_id
),

-- Handle product_id lookup and set to 0 if missing
product_validation AS (
    SELECT
        ov.order_item_id,
        ov.order_id,
        COALESCE(dp.product_id, '0') AS product_id,
        ov.quantity,
        ov.unit_price
    FROM order_validation ov
    LEFT JOIN {{ ref('dim_product') }} dp ON ov.product_id = dp.product_id
),

transformed AS (
    SELECT
        pv.order_item_id AS foi_order_item_id,
        pv.order_id AS foi_order_id,
        pv.product_id AS foi_product_id,
        -- Ensure integer semantics for quantity
        ROUND(pv.quantity, 0) AS foi_quantity,
        -- Direct mapping for unit_price
        pv.unit_price AS foi_unit_price,
        -- Compute extended_amount (quantity * unit_price, treating null unit_price as 0)
        ROUND(pv.quantity, 0) * COALESCE(pv.unit_price, 0) AS foi_extended_amount,
        -- Set discount_amount to 0 (no discount context available)
        0.00 AS foi_discount_amount,
        -- Compute net_amount (extended_amount - discount_amount, floor at 0)
        GREATEST(
            (ROUND(pv.quantity, 0) * COALESCE(pv.unit_price, 0)) - 0.00,
            0.00
        ) AS foi_net_amount
    FROM product_validation pv
)

SELECT * FROM transformed