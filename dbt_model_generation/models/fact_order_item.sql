SOURCE TABLES: bsl_raw.dwh_raw.order_items, bsl_raw.dwh_raw.orders
DESCRIPTION: This model creates a fact table for order items by transforming raw order item data with calculated fields like extended amount and net amount. It includes lookups to validate order and product references.
PREREQUISITES: fact_order and dim_product models should exist for reference validation
PARAMETER: Incremental materialization with conditional loading
