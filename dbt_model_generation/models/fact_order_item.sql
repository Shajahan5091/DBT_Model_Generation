{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

with source_order_items as (
    select 
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
    where exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

source_orders as (
    select 
        order_id
    from {{ source('dwh_raw', 'orders') }}
),

-- Validate order_id exists in fact_order
validated_orders as (
    select 
        oi.*
    from source_order_items oi
    inner join {{ ref('fact_order') }} fo 
        on oi.order_id = fo.order_id
),

-- Validate product_id exists in dim_product, set to 0 if missing
validated_products as (
    select 
        vo.*,
        coalesce(dp.product_id, '0') as validated_product_id
    from validated_orders vo
    left join {{ ref('dim_product') }} dp 
        on vo.product_id = dp.product_id
),

-- Calculate extended amount and other derived fields
final as (
    select 
        vp.order_item_id,
        vp.order_id,
        vp.validated_product_id as product_id,
        round(vp.quantity) as quantity,  -- Ensure integer semantics
        vp.unit_price::number(10,2) as unit_price,
        (round(vp.quantity) * coalesce(vp.unit_price, 0))::number(12,2) as extended_amount,
        0::number(12,2) as discount_amount,  -- Set to 0 as no discount context available
        greatest(0, (round(vp.quantity) * coalesce(vp.unit_price, 0)) - 0)::number(12,2) as net_amount  -- Ensure non-negative
    from validated_products vp
)

select * from final