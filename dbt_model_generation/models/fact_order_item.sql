WITH source_order_items AS (
    SELECT
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price
    FROM {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
    WHERE exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

source_orders AS (
    SELECT
        order_id
    FROM {{ source('dwh_raw', 'orders') }}
),

-- Validate order_id exists in fact_order
validated_orders AS (
    SELECT
        oi.order_item_id,
        oi.order_id,
        oi.product_id,
        oi.quantity,
        oi.unit_price
    FROM source_order_items oi
    INNER JOIN {{ ref('fact_order') }} fo ON oi.order_id = fo.order_id
),

-- Handle product_id lookup and set to 0 if missing
product_validated AS (
    SELECT
        vo.order_item_id,
        vo.order_id,
        COALESCE(dp.product_id, '0') AS product_id,
        vo.quantity,
        vo.unit_price
    FROM validated_orders vo
    LEFT JOIN {{ ref('dim_product') }} dp ON vo.product_id = dp.product_id
),

transformed AS (
    SELECT
        order_item_id,
        order_id,
        product_id,
        -- Ensure integer semantics for quantity
        ROUND(quantity, 0) AS quantity,
        -- Direct mapping for unit_price
        unit_price,
        -- Calculate extended_amount (quantity * unit_price, treating null unit_price as 0)
        ROUND(quantity, 0) * COALESCE(unit_price, 0) AS extended_amount,
        -- Set discount_amount to 0 (no discount context provided)
        0.00 AS discount_amount,
        -- Calculate net_amount (extended_amount - discount_amount, floor at 0)
        GREATEST(
            (ROUND(quantity, 0) * COALESCE(unit_price, 0)) - 0.00,
            0.00
        ) AS net_amount
    FROM product_validated
)

SELECT * FROM transformed