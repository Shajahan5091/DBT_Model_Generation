MODEL NAME: fact_order_item
TARGET TABLE: BSL_MA.DWH_MA.FACT_ORDER_ITEM
SOURCE TABLES: BSL_RAW.DWH_RAW.ORDER_ITEMS
DESCRIPTION: This model transforms raw order item data into a fact table with calculated extended amounts, discount allocations, and net amounts. It includes lookups to validate orders and products.
PREREQUISITES: fact_order and dim_product tables must exist and be populated
PARAMETER: None
AUTHOR: AI Generated by Shajahan - 2024-12-19 IST
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

with source_data as (
    select *
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
        where exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

validated_orders as (
    select 
        s.*
    from source_data s
    inner join {{ ref('fact_order') }} fo
        on s.order_id = fo.order_id
    -- Skip items if order_id missing in fact_order
),

product_lookup as (
    select 
        vo.*,
        coalesce(dp.product_id, '0') as validated_product_id
    from validated_orders vo
    left join {{ ref('dim_product') }} dp
        on vo.product_id = dp.product_id
),

calculated_amounts as (
    select 
        order_item_id,
        order_id,
        validated_product_id as product_id,
        -- Ensure integer semantics; round if fractional
        round(quantity) as quantity,
        unit_price,
        -- Compute extended_amount as quantity * unit_price
        round(quantity) * coalesce(unit_price, 0) as extended_amount,
        -- Set discount_amount to 0 if no discount context
        0.00 as discount_amount
    from product_lookup
),

final as (
    select 
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price::number(10,2) as unit_price,
        extended_amount::number(12,2) as extended_amount,
        discount_amount::number(12,2) as discount_amount,
        -- Compute net_amount as extended_amount - discount_amount, ensure non-negative
        greatest(0, extended_amount - discount_amount)::number(12,2) as net_amount
    from calculated_amounts
)

select * from final