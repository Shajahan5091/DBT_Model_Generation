{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

WITH source_order_items AS (
    SELECT
        oi.order_item_id,
        oi.order_id,
        oi.product_id,
        oi.quantity,
        oi.unit_price
    FROM {{ source('dwh_raw', 'order_items') }} oi
    {% if is_incremental() %}
        WHERE exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

source_orders AS (
    SELECT
        o.order_id
    FROM {{ source('dwh_raw', 'orders') }} o
),

fact_order_lookup AS (
    SELECT
        fo.order_id
    FROM {{ ref('fact_order') }} fo
),

dim_product_lookup AS (
    SELECT
        dp.product_id
    FROM {{ ref('dim_product') }} dp
),

transformed AS (
    SELECT
        oi.order_item_id,
        oi.order_id,
        CASE 
            WHEN dp.product_id IS NULL THEN '0'
            ELSE oi.product_id
        END AS product_id,
        ROUND(oi.quantity) AS quantity,
        oi.unit_price,
        ROUND(oi.quantity) * COALESCE(oi.unit_price, 0) AS extended_amount,
        0.00 AS discount_amount,
        GREATEST(0, ROUND(oi.quantity) * COALESCE(oi.unit_price, 0) - 0.00) AS net_amount
    FROM source_order_items oi
    INNER JOIN fact_order_lookup fo ON oi.order_id = fo.order_id
    LEFT JOIN dim_product_lookup dp ON oi.product_id = dp.product_id
)

SELECT * FROM transformed