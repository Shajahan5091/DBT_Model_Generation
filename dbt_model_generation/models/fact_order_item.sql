{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

with source_data as (
    select *
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
    where exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

order_lookup as (
    select 
        order_id,
        -- Add any discount information from fact_order if available
        coalesce(total_discount_amount, 0) as order_discount_amount,
        coalesce(total_order_amount, 0) as total_order_amount
    from {{ ref('fact_order') }}
),

product_lookup as (
    select 
        product_id,
        product_sk
    from {{ ref('dim_product') }}
),

transformed_data as (
    select 
        s.order_item_id,
        -- Order ID lookup with skip logic for missing orders
        case 
            when o.order_id is not null then cast(s.order_id as integer)
            else null
        end as order_id,
        
        -- Product ID lookup with unknown handling
        case 
            when p.product_id is not null then cast(s.product_id as integer)
            else 0  -- Set to 0 (unknown) if product missing
        end as product_id,
        
        coalesce(p.product_sk, 0) as product_sk,
        
        -- Quantity with integer semantics
        round(coalesce(s.quantity, 0)) as quantity,
        
        -- Unit price direct mapping
        cast(coalesce(s.unit_price, 0) as number(10,2)) as unit_price,
        
        -- Extended amount calculation
        cast(
            round(coalesce(s.quantity, 0)) * coalesce(s.unit_price, 0) 
            as number(12,2)
        ) as extended_amount,
        
        -- Discount amount allocation (proportional if order-level discount exists)
        cast(
            case 
                when o.order_discount_amount > 0 and o.total_order_amount > 0 then
                    (round(coalesce(s.quantity, 0)) * coalesce(s.unit_price, 0)) * 
                    (o.order_discount_amount / o.total_order_amount)
                else 0
            end as number(12,2)
        ) as discount_amount
        
    from source_data s
    left join order_lookup o on s.order_id = o.order_id
    left join product_lookup p on s.product_id = p.product_id
    -- Skip items if order_id missing in fact_order
    where o.order_id is not null
),

final as (
    select 
        order_item_id,
        order_id,
        product_id,
        product_sk,
        quantity,
        unit_price,
        extended_amount,
        discount_amount,
        -- Net amount calculation with non-negative constraint
        cast(
            greatest(0, extended_amount - discount_amount) 
            as number(12,2)
        ) as net_amount
    from transformed_data
)

select * from final