{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

with source_data as (
    select 
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
    where exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

order_lookup as (
    select distinct order_id
    from {{ ref('fact_order') }}
),

product_lookup as (
    select distinct product_id
    from {{ ref('dim_product') }}
),

transformed_data as (
    select 
        src.order_item_id,
        src.order_id,
        -- Handle missing products by setting to 0
        case 
            when pl.product_id is not null then src.product_id
            else '0'
        end as product_id,
        -- Ensure integer semantics for quantity
        round(src.quantity) as quantity,
        src.unit_price,
        -- Calculate extended amount, treating null unit_price as 0
        round(src.quantity) * coalesce(src.unit_price, 0) as extended_amount,
        -- Set discount amount to 0 (no discount context available)
        0.00 as discount_amount
    from source_data src
    left join order_lookup ol on src.order_id = ol.order_id
    left join product_lookup pl on src.product_id = pl.product_id
    -- Skip items if order_id missing in fact_order
    where ol.order_id is not null
),

final as (
    select 
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price,
        extended_amount,
        discount_amount,
        -- Calculate net amount ensuring non-negative value
        greatest(extended_amount - discount_amount, 0) as net_amount
    from transformed_data
)

select * from final