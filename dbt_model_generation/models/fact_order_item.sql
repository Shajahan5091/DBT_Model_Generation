MODEL NAME: fact_order_item
TARGET TABLE: bsl_ma.dwh_ma.fact_order_item
SOURCE TABLES: bsl_raw.dwh_raw.order_items, bsl_raw.dwh_raw.orders
DESCRIPTION: This model transforms raw order item data into a fact table containing order item details with calculated amounts, discount allocations, and product lookups
PREREQUISITES: fact_order and dim_product models must exist for proper joins
PARAMETER: None
AUTHOUR: AI Generated by Shajahan - 2024-12-19
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

WITH source_order_items AS (
    SELECT
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price
    FROM {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
        WHERE exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

source_orders AS (
    SELECT
        order_id
    FROM {{ source('dwh_raw', 'orders') }}
),

fact_order_lookup AS (
    SELECT
        order_id
    FROM {{ ref('fact_order') }}
),

dim_product_lookup AS (
    SELECT
        product_id
    FROM {{ ref('dim_product') }}
),

transformed AS (
    SELECT
        oi.order_item_id,
        oi.order_id,
        CASE 
            WHEN dp.product_id IS NULL THEN '0'
            ELSE oi.product_id
        END AS product_id,
        ROUND(oi.quantity) AS quantity,
        oi.unit_price,
        (ROUND(oi.quantity) * COALESCE(oi.unit_price, 0)) AS extended_amount,
        0 AS discount_amount,
        GREATEST(0, (ROUND(oi.quantity) * COALESCE(oi.unit_price, 0)) - 0) AS net_amount
    FROM source_order_items oi
    INNER JOIN fact_order_lookup fo ON oi.order_id = fo.order_id
    LEFT JOIN dim_product_lookup dp ON oi.product_id = dp.product_id
)

SELECT * FROM transformed