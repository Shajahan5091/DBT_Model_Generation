WITH source_order_items AS (
    SELECT
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price
    FROM {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
        WHERE exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

source_orders AS (
    SELECT
        order_id
    FROM {{ source('dwh_raw', 'orders') }}
),

-- Validate order_id exists in fact_order
validated_orders AS (
    SELECT 
        oi.*
    FROM source_order_items oi
    INNER JOIN {{ ref('fact_order') }} fo ON oi.order_id = fo.order_id
),

-- Handle product lookups with default fallback
product_validated AS (
    SELECT 
        vo.*,
        COALESCE(dp.product_id, '0') as validated_product_id
    FROM validated_orders vo
    LEFT JOIN {{ ref('dim_product') }} dp ON vo.product_id = dp.product_id
),

-- Calculate extended amounts and handle discount allocation
transformed AS (
    SELECT
        -- Direct mappings
        order_item_id,
        order_id,
        validated_product_id as product_id,
        ROUND(quantity) as quantity, -- Ensure integer semantics
        unit_price,
        
        -- Calculated fields
        (ROUND(quantity) * COALESCE(unit_price, 0)) as extended_amount,
        0 as discount_amount, -- Set to 0 as no discount context available
        GREATEST(0, (ROUND(quantity) * COALESCE(unit_price, 0)) - 0) as net_amount -- Ensure non-negative, floor at 0
        
    FROM product_validated
)

SELECT * FROM transformed