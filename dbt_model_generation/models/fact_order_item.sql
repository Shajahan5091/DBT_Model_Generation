------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

with source_data as (
    select 
        order_id,
        product_id,
        quantity,
        unit_price,
        order_item_id
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
    where exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

-- Filter out records where order_id is missing in fact_order
valid_orders as (
    select 
        s.*
    from source_data s
    inner join {{ ref('fact_order') }} fo
        on s.order_id = fo.order_id
),

-- Handle product lookups and set missing products to 0
product_lookup as (
    select 
        vo.order_id,
        coalesce(dp.product_id, '0') as product_id,
        vo.quantity,
        vo.unit_price,
        vo.order_item_id
    from valid_orders vo
    left join {{ ref('dim_product') }} dp
        on vo.product_id = dp.product_id
),

-- Calculate extended amount and apply business rules
calculated_amounts as (
    select 
        order_id,
        product_id,
        -- Ensure integer semantics for quantity
        round(quantity) as quantity,
        unit_price,
        order_item_id,
        -- Calculate extended amount, treating null unit_price as 0
        round(quantity) * coalesce(unit_price, 0) as extended_amount,
        -- Set discount to 0 as no discount context available
        0.00 as discount_amount
    from product_lookup
),

-- Calculate final net amount ensuring non-negative values
final_calculations as (
    select 
        order_id,
        product_id,
        quantity,
        cast(unit_price as number(10,2)) as unit_price,
        order_item_id,
        cast(extended_amount as number(12,2)) as extended_amount,
        cast(discount_amount as number(12,2)) as discount_amount,
        -- Ensure net amount is non-negative, floor at 0
        cast(greatest(extended_amount - discount_amount, 0) as number(12,2)) as net_amount
    from calculated_amounts
)

select 
    order_id,
    product_id,
    quantity,
    unit_price,
    extended_amount,
    discount_amount,
    net_amount
from final_calculations