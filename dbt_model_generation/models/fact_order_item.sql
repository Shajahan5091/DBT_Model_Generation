MODEL NAME: fact_order_item
TARGET TABLE: BSL_MA.DWH_MA.FACT_ORDER_ITEM
SOURCE TABLES: BSL_RAW.DWH_RAW.ORDER_ITEMS
DESCRIPTION: This model transforms raw order item data into a fact table with calculated extended amounts, discount allocations, and net amounts. It includes lookups to validate orders and products.
PREREQUISITES: fact_order and dim_product tables must exist and be populated
PARAMETER: None
AUTHOR: AI Generated by Shajahan - 2024-12-19
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

with source_data as (
    select 
        order_id,
        product_id,
        quantity,
        unit_price,
        order_item_id
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
    where exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

validated_orders as (
    select 
        sd.order_id,
        sd.product_id,
        sd.quantity,
        sd.unit_price,
        sd.order_item_id
    from source_data sd
    inner join {{ ref('fact_order') }} fo 
        on sd.order_id = fo.order_id
),

product_lookup as (
    select 
        vo.order_id,
        coalesce(dp.product_id, '0') as product_id,
        round(vo.quantity) as quantity,
        coalesce(vo.unit_price, 0) as unit_price,
        vo.order_item_id
    from validated_orders vo
    left join {{ ref('dim_product') }} dp 
        on vo.product_id = dp.product_id
),

calculations as (
    select 
        order_id,
        product_id,
        quantity,
        unit_price,
        order_item_id,
        (quantity * unit_price) as extended_amount,
        0 as discount_amount
    from product_lookup
),

final as (
    select 
        order_id,
        product_id,
        quantity,
        unit_price,
        extended_amount,
        discount_amount,
        greatest(0, extended_amount - discount_amount) as net_amount
    from calculations
)

select * from final