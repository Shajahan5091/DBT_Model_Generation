MODEL NAME: fact_order_item
TARGET TABLE: bsl_ma.dwh_ma.fact_order_item
SOURCE TABLES: bsl_raw.dwh_raw.order_items
DESCRIPTION: This model transforms raw order item data into a fact table with calculated extended amounts, discount allocations, and net amounts. It performs lookups to validate orders and products while handling missing data appropriately.
PREREQUISITES: fact_order and dim_product models must exist and be populated
PARAMETER: None
