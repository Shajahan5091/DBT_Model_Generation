{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

with source_data as (
    select *
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
    where exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

order_lookup as (
    select *
    from {{ ref('fact_order') }}
),

product_lookup as (
    select *
    from {{ ref('dim_product') }}
),

transformed_data as (
    select
        s.order_item_id,
        -- Order ID lookup with skip logic for missing orders
        case 
            when o.order_id is not null then cast(o.order_id as integer)
            else null
        end as order_id,
        
        -- Product ID lookup with unknown handling
        case 
            when p.product_id is not null then s.product_id
            else s.product_id
        end as product_id,
        
        case 
            when p.product_id is not null then p.product_sk
            else 0  -- Set to 0 (unknown) if product missing
        end as product_sk,
        
        -- Quantity with integer semantics
        round(coalesce(s.quantity, 0)) as quantity,
        
        -- Unit price direct mapping
        cast(coalesce(s.unit_price, 0) as number(10,2)) as unit_price,
        
        -- Extended amount calculation
        cast(
            round(coalesce(s.quantity, 0)) * coalesce(s.unit_price, 0)
            as number(12,2)
        ) as extended_amount,
        
        -- Discount amount allocation (set to 0 for now as no discount context)
        cast(0 as number(12,2)) as discount_amount,
        
        -- Net amount calculation ensuring non-negative
        cast(
            greatest(
                0,
                (round(coalesce(s.quantity, 0)) * coalesce(s.unit_price, 0)) - 0
            ) as number(12,2)
        ) as net_amount
        
    from source_data s
    left join order_lookup o on s.order_id = o.order_id
    left join product_lookup p on s.product_id = p.product_id
    
    -- Skip items if order_id missing in fact_order
    where o.order_id is not null
)

select * from transformed_data