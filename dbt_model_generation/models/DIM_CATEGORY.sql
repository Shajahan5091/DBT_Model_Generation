WITH source_data AS (
    SELECT 
        category_id,
        category_name,
        parent_category_id,
        is_active
    FROM {{ source('dwh_raw', 'categories') }}
),

-- Generate surrogate keys for categories
category_with_sk AS (
    SELECT 
        {{ dbt_utils.generate_surrogate_key(['category_id']) }} AS category_sk,
        category_id,
        TRIM(INITCAP(category_name)) AS category_name,
        parent_category_id,
        COALESCE(is_active, TRUE) AS is_active
    FROM source_data
),

-- Self-join to get parent category information for surrogate key lookup
category_with_parent AS (
    SELECT 
        c.category_sk,
        c.category_id,
        c.category_name,
        c.parent_category_id,
        p.category_sk AS parent_category_sk,
        p.category_name AS parent_category_name,
        c.is_active
    FROM category_with_sk c
    LEFT JOIN category_with_sk p 
        ON c.parent_category_id = p.category_id
),

-- Build category path (up to 2 levels)
final AS (
    SELECT 
        category_sk,
        category_id,
        category_name,
        parent_category_id,
        parent_category_sk,
        CASE 
            WHEN parent_category_name IS NOT NULL 
            THEN parent_category_name || ' > ' || category_name
            ELSE category_name
        END AS category_path,
        is_active
    FROM category_with_parent
)

SELECT 
    category_sk,
    category_id,
    category_name,
    parent_category_id,
    parent_category_sk,
    category_path,
    is_active
FROM final