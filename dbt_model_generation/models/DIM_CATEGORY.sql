with source_data as (
    select * from {{ source('dwh_raw', 'categories') }}
),

parent_categories as (
    select 
        category_id,
        category_name
    from source_data
),

transformed as (
    select
        -- Direct mapping with unique test
        s.category_id,
        
        -- Trim and title-case transformation
        initcap(trim(s.category_name)) as category_name,
        
        -- Direct mapping with null handling for root categories
        s.parent_category_id,
        
        -- Build hierarchical path up to 2 levels
        case 
            when s.parent_category_id is not null and p.category_name is not null then
                initcap(trim(p.category_name)) || ' > ' || initcap(trim(s.category_name))
            else
                initcap(trim(s.category_name))
        end as category_path,
        
        -- Direct mapping with null default handling
        coalesce(s.is_active, true) as is_active
        
    from source_data s
    left join parent_categories p
        on s.parent_category_id = p.category_id
)

select * from transformed