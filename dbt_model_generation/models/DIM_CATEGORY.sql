with source_data as (
    select * from {{ source('dwh_raw', 'categories') }}
),

parent_categories as (
    select 
        category_id,
        trim(initcap(category_name)) as parent_category_name
    from source_data
),

transformed as (
    select
        -- Direct mapping for category_id
        s.category_id,
        
        -- Trim and title-case for category_name
        trim(initcap(s.category_name)) as category_name,
        
        -- Direct mapping for parent_category_id
        s.parent_category_id,
        
        -- Build category path up to 2 levels
        case 
            when s.parent_category_id is not null and p.parent_category_name is not null
            then p.parent_category_name || ' > ' || trim(initcap(s.category_name))
            else trim(initcap(s.category_name))
        end as category_path,
        
        -- Direct mapping for is_active with null handling
        coalesce(s.is_active, true) as is_active
        
    from source_data s
    left join parent_categories p 
        on s.parent_category_id = p.category_id
)

select * from transformed