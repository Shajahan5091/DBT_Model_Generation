{{ config(materialized='table') }}

with source_data as (
    select 
        product_id,
        supplier_id,
        stock_qty,
        warehouse_location,
        last_updated
    from {{ source('dwh_raw', 'inventory') }}
),

-- Add row numbers to identify latest records per composite key
ranked_data as (
    select 
        *,
        row_number() over (
            partition by product_id, supplier_id 
            order by last_updated desc
        ) as rn
    from source_data
),

-- Apply transformations and data quality rules
transformed_data as (
    select 
        -- Product ID lookup with unknown handling
        case 
            when product_id is null or product_id = '' then '0'
            else product_id 
        end as product_id,
        
        -- Supplier ID lookup with unknown handling  
        case 
            when supplier_id is null or supplier_id = '' then '0'
            else supplier_id 
        end as supplier_id,
        
        -- Stock quantity with negative value handling
        case 
            when stock_qty < 0 then null
            else stock_qty 
        end as stock_qty,
        
        -- Warehouse location uppercase transformation
        upper(warehouse_location) as warehouse_location,
        
        -- Convert timestamp to date
        last_updated::date as snapshot_date,
        
        -- Flag latest record per composite key
        case when rn = 1 then true else false end as is_latest,
        
        last_updated
    from ranked_data
)

select 
    product_id,
    supplier_id, 
    stock_qty,
    warehouse_location,
    snapshot_date,
    is_latest
from transformed_data