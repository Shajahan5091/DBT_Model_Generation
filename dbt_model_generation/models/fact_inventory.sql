{{ config(
    materialized = 'table',
    alias = 'fact_inventory'
) }}

WITH source_data AS (
    SELECT
        inv.product_id,
        inv.supplier_id,
        inv.stock_qty,
        inv.warehouse_location,
        inv.last_updated
    FROM {{ source('dwh_raw', 'inventory') }} inv
),

product_lookup AS (
    SELECT
        src.product_id,
        src.supplier_id,
        src.stock_qty,
        src.warehouse_location,
        src.last_updated,
        COALESCE(dp.product_id, '0') as final_product_id,
        CASE WHEN dp.product_id IS NULL THEN TRUE ELSE FALSE END as product_missing_flag
    FROM source_data src
    LEFT JOIN {{ ref('dim_product') }} dp ON src.product_id = dp.product_id
),

supplier_lookup AS (
    SELECT
        pl.final_product_id as product_id,
        COALESCE(ds.supplier_id, '0') as supplier_id,
        pl.stock_qty,
        pl.warehouse_location,
        pl.last_updated,
        pl.product_missing_flag,
        CASE WHEN ds.supplier_id IS NULL THEN TRUE ELSE FALSE END as supplier_missing_flag
    FROM product_lookup pl
    LEFT JOIN {{ ref('dim_supplier') }} ds ON pl.supplier_id = ds.supplier_id
),

stock_validation AS (
    SELECT
        sl.product_id,
        sl.supplier_id,
        CASE 
            WHEN sl.stock_qty < 0 THEN NULL 
            ELSE sl.stock_qty 
        END as stock_qty,
        CASE 
            WHEN sl.stock_qty < 0 THEN TRUE 
            ELSE FALSE 
        END as negative_stock_flag,
        UPPER(sl.warehouse_location) as warehouse_location,
        sl.last_updated,
        sl.product_missing_flag,
        sl.supplier_missing_flag
    FROM supplier_lookup sl
),

latest_flag AS (
    SELECT
        sv.product_id,
        sv.supplier_id,
        sv.stock_qty,
        sv.warehouse_location,
        DATE(sv.last_updated) as snapshot_date,
        CASE 
            WHEN sv.last_updated = MAX(sv.last_updated) OVER (PARTITION BY sv.product_id, sv.supplier_id) 
            THEN TRUE 
            ELSE FALSE 
        END as is_latest,
        sv.negative_stock_flag,
        sv.product_missing_flag,
        sv.supplier_missing_flag
    FROM stock_validation sv
),

transformed AS (
    SELECT
        product_id,
        supplier_id,
        stock_qty,
        warehouse_location,
        snapshot_date,
        is_latest,
        negative_stock_flag,
        product_missing_flag,
        supplier_missing_flag
    FROM latest_flag
)

SELECT * FROM transformed