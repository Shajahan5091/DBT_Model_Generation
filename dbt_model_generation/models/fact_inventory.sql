{{ config(
    materialized = 'table',
    schema = 'dwh_ma',
    alias = 'fact_inventory'
) }}

WITH source_data AS (
    SELECT
        product_id,
        supplier_id,
        stock_qty,
        warehouse_location,
        last_updated
    FROM {{ source('dwh_raw', 'inventory') }}
),

product_lookup AS (
    SELECT
        product_id
    FROM {{ ref('dim_product') }}
),

supplier_lookup AS (
    SELECT
        supplier_id  
    FROM {{ ref('dim_supplier') }}
),

latest_records AS (
    SELECT
        product_id,
        supplier_id,
        MAX(last_updated) AS max_last_updated
    FROM source_data
    GROUP BY product_id, supplier_id
),

transformed AS (
    SELECT
        -- Product ID lookup with unknown handling
        COALESCE(p.product_id, s.product_id, '0') AS product_id,
        
        -- Supplier ID lookup with unknown handling  
        COALESCE(sup.supplier_id, s.supplier_id, '0') AS supplier_id,
        
        -- Stock quantity with negative value handling
        CASE 
            WHEN s.stock_qty < 0 THEN NULL
            ELSE s.stock_qty
        END AS stock_qty,
        
        -- Warehouse location uppercase transformation
        UPPER(s.warehouse_location) AS warehouse_location,
        
        -- Convert timestamp to date
        DATE(s.last_updated) AS snapshot_date,
        
        -- Flag for latest record per product_id + supplier_id
        CASE 
            WHEN s.last_updated = lr.max_last_updated THEN TRUE
            ELSE FALSE
        END AS is_latest
        
    FROM source_data s
    LEFT JOIN product_lookup p ON s.product_id = p.product_id
    LEFT JOIN supplier_lookup sup ON s.supplier_id = sup.supplier_id
    LEFT JOIN latest_records lr ON s.product_id = lr.product_id 
                                AND s.supplier_id = lr.supplier_id
)

SELECT * FROM transformed