{{ config(materialized='table') }}

with source_data as (
    select 
        product_id,
        supplier_id,
        stock_qty,
        warehouse_location,
        last_updated
    from {{ source('dwh_raw', 'inventory') }}
),

product_lookup as (
    select 
        product_id
    from {{ ref('dim_product') }}
),

supplier_lookup as (
    select 
        supplier_id  
    from {{ ref('dim_supplier') }}
),

transformed_data as (
    select
        -- Product ID with unknown handling
        case 
            when p.product_id is not null then s.product_id
            else '0'
        end as product_id,
        
        -- Supplier ID with unknown handling  
        case 
            when sup.supplier_id is not null then s.supplier_id
            else '0'
        end as supplier_id,
        
        -- Stock quantity with negative value handling
        case 
            when s.stock_qty < 0 then null
            else s.stock_qty
        end as stock_qty,
        
        -- Warehouse location in uppercase
        upper(s.warehouse_location) as warehouse_location,
        
        -- Convert timestamp to date
        s.last_updated::date as snapshot_date,
        
        -- Flag for latest record per product_id + supplier_id combination
        case 
            when s.last_updated = max(s.last_updated) over (
                partition by s.product_id, s.supplier_id
            ) then true
            else false
        end as is_latest
        
    from source_data s
    left join product_lookup p on s.product_id = p.product_id
    left join supplier_lookup sup on s.supplier_id = sup.supplier_id
)

select * from transformed_data