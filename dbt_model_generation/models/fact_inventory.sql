{{ config(
    materialized = 'table',
    alias = 'fact_inventory'
) }}

WITH source_data AS (
    SELECT
        product_id,
        supplier_id,
        stock_qty,
        warehouse_location,
        last_updated
    FROM {{ source('dwh_raw', 'inventory') }}
),

product_lookup AS (
    SELECT
        product_id AS dim_product_id
    FROM {{ ref('dim_product') }}
),

supplier_lookup AS (
    SELECT
        supplier_id AS dim_supplier_id
    FROM {{ ref('dim_supplier') }}
),

latest_records AS (
    SELECT
        product_id,
        supplier_id,
        stock_qty,
        warehouse_location,
        last_updated,
        ROW_NUMBER() OVER (
            PARTITION BY product_id, supplier_id 
            ORDER BY last_updated DESC
        ) AS rn
    FROM source_data
),

transformed AS (
    SELECT
        -- Product ID lookup with unknown handling
        COALESCE(pl.dim_product_id, '0') AS inv_product_id,
        
        -- Supplier ID lookup with unknown handling
        COALESCE(sl.dim_supplier_id, '0') AS inv_supplier_id,
        
        -- Stock quantity with negative value handling
        CASE 
            WHEN lr.stock_qty < 0 THEN NULL 
            ELSE lr.stock_qty 
        END AS inv_stock_qty,
        
        -- Warehouse location uppercase transformation
        UPPER(lr.warehouse_location) AS inv_warehouse_location,
        
        -- Convert timestamp to date
        DATE(lr.last_updated) AS inv_snapshot_date,
        
        -- Flag for latest record
        CASE WHEN lr.rn = 1 THEN TRUE ELSE FALSE END AS inv_is_latest
        
    FROM latest_records lr
    LEFT JOIN product_lookup pl ON lr.product_id = pl.dim_product_id
    LEFT JOIN supplier_lookup sl ON lr.supplier_id = sl.dim_supplier_id
)

SELECT * FROM transformed