WITH source_data AS (
    SELECT
        product_id,
        supplier_id,
        stock_qty,
        warehouse_location,
        last_updated
    FROM {{ source('dwh_raw', 'inventory') }}
),

product_lookup AS (
    SELECT
        product_id
    FROM {{ ref('dim_product') }}
),

supplier_lookup AS (
    SELECT
        supplier_id
    FROM {{ ref('dim_supplier') }}
),

latest_records AS (
    SELECT
        product_id,
        supplier_id,
        stock_qty,
        warehouse_location,
        last_updated,
        ROW_NUMBER() OVER (
            PARTITION BY product_id, supplier_id 
            ORDER BY last_updated DESC
        ) = 1 AS is_latest
    FROM source_data
),

transformed AS (
    SELECT
        -- Product ID lookup with unknown handling
        COALESCE(p.product_id, lr.product_id, '0') AS product_id,
        
        -- Supplier ID lookup with unknown handling
        COALESCE(s.supplier_id, lr.supplier_id, '0') AS supplier_id,
        
        -- Stock quantity with negative value handling
        CASE 
            WHEN lr.stock_qty < 0 THEN NULL 
            ELSE lr.stock_qty 
        END AS stock_qty,
        
        -- Warehouse location uppercase transformation
        UPPER(lr.warehouse_location) AS warehouse_location,
        
        -- Convert timestamp to date
        lr.last_updated::DATE AS snapshot_date,
        
        -- Latest record flag
        lr.is_latest
        
    FROM latest_records lr
    LEFT JOIN product_lookup p ON lr.product_id = p.product_id
    LEFT JOIN supplier_lookup s ON lr.supplier_id = s.supplier_id
)

SELECT * FROM transformed