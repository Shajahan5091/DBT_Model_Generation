{{ config(
    materialized = 'incremental',
    unique_key = 'payment_id'
) }}

WITH payments_source AS (
    SELECT
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount
    FROM {{ source('dwh_raw', 'payments') }}
    {% if is_incremental() %}
        WHERE payment_date::date > (select coalesce(max(payment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

orders_source AS (
    SELECT
        order_id,
        total_amount
    FROM {{ source('dwh_raw', 'orders') }}
),

fact_order_ref AS (
    SELECT
        order_id,
        total_amount
    FROM {{ ref('fact_order') }}
),

payment_order_joined AS (
    SELECT
        payments_source.payment_id,
        payments_source.order_id,
        payments_source.payment_date::date AS payment_date,
        CASE 
            WHEN UPPER(payments_source.payment_method) IN ('CARD', 'UPI', 'COD', 'NETBANKING') 
            THEN UPPER(payments_source.payment_method)
            ELSE 'CARD'
        END AS payment_method,
        CASE 
            WHEN payments_source.amount < 0 THEN NULL
            ELSE payments_source.amount
        END AS amount,
        fact_order_ref.total_amount AS order_total_amount
    FROM payments_source
    INNER JOIN fact_order_ref
        ON payments_source.order_id = fact_order_ref.order_id
),

payment_aggregated AS (
    SELECT
        payment_order_joined.order_id,
        SUM(payment_order_joined.amount) AS total_payments
    FROM payment_order_joined
    WHERE payment_order_joined.amount IS NOT NULL
    GROUP BY payment_order_joined.order_id
),

final_transformed AS (
    SELECT
        payment_order_joined.payment_id,
        payment_order_joined.order_id,
        payment_order_joined.payment_date,
        payment_order_joined.payment_method,
        payment_order_joined.amount,
        CASE 
            WHEN payment_order_joined.order_total_amount IS NULL THEN FALSE
            WHEN payment_aggregated.total_payments >= payment_order_joined.order_total_amount THEN TRUE
            ELSE FALSE
        END AS is_fully_applied
    FROM payment_order_joined
    LEFT JOIN payment_aggregated
        ON payment_order_joined.order_id = payment_aggregated.order_id
)

SELECT * FROM final_transformed