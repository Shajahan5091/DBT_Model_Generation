{{ config(
    materialized = 'incremental',
    unique_key = 'payment_id'
) }}

WITH source_payments AS (
    SELECT
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount
    FROM {{ source('dwh_raw', 'payments') }}
    {% if is_incremental() %}
        WHERE payment_date::date > (select coalesce(max(payment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

source_orders AS (
    SELECT
        order_id,
        total_amount
    FROM {{ source('dwh_raw', 'orders') }}
),

order_payments_summary AS (
    SELECT
        sp.order_id,
        SUM(CASE WHEN sp.amount >= 0 THEN sp.amount ELSE 0 END) as total_payments
    FROM source_payments sp
    GROUP BY sp.order_id
),

transformed AS (
    SELECT
        sp.payment_id as payment_id,
        sp.order_id as order_id,
        sp.payment_date::date as payment_date,
        CASE 
            WHEN UPPER(sp.payment_method) IN ('CARD', 'UPI', 'COD', 'NETBANKING') 
            THEN UPPER(sp.payment_method)
            ELSE 'CARD'
        END as payment_method,
        CASE 
            WHEN sp.amount < 0 THEN NULL
            ELSE sp.amount
        END as amount,
        CASE 
            WHEN so.total_amount IS NULL THEN FALSE
            WHEN ops.total_payments >= so.total_amount THEN TRUE
            ELSE FALSE
        END as is_fully_applied
    FROM source_payments sp
    INNER JOIN source_orders so ON sp.order_id = so.order_id
    LEFT JOIN order_payments_summary ops ON sp.order_id = ops.order_id
)

SELECT * FROM transformed