{{ config(
    materialized = 'incremental',
    unique_key = 'payment_id'
) }}

WITH payments_source AS (
    SELECT
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount
    FROM {{ source('dwh_raw', 'payments') }}
    {% if is_incremental() %}
        WHERE payment_date::date > (select coalesce(max(payment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

orders_source AS (
    SELECT
        order_id,
        total_amount
    FROM {{ source('dwh_raw', 'orders') }}
),

fact_order_ref AS (
    SELECT
        order_id,
        total_amount
    FROM {{ ref('fact_order') }}
),

payment_aggregation AS (
    SELECT
        p.order_id,
        SUM(CASE WHEN p.amount >= 0 THEN p.amount ELSE NULL END) as total_payments
    FROM payments_source p
    GROUP BY p.order_id
),

transformed AS (
    SELECT
        p.payment_id,
        p.order_id,
        p.payment_date::date as payment_date,
        CASE 
            WHEN UPPER(p.payment_method) IN ('CARD', 'UPI', 'COD', 'NETBANKING') 
            THEN UPPER(p.payment_method)
            ELSE 'CARD'
        END as payment_method,
        CASE 
            WHEN p.amount < 0 THEN NULL
            ELSE p.amount
        END as amount,
        CASE 
            WHEN fo.total_amount IS NULL THEN FALSE
            WHEN pa.total_payments >= fo.total_amount THEN TRUE
            ELSE FALSE
        END as is_fully_applied
    FROM payments_source p
    LEFT JOIN fact_order_ref fo ON p.order_id = fo.order_id
    LEFT JOIN payment_aggregation pa ON p.order_id = pa.order_id
    WHERE fo.order_id IS NOT NULL
)

SELECT * FROM transformed