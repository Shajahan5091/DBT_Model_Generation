{{ config(
    materialized='incremental',
    unique_key='payment_id',
    incremental_strategy='merge'
) }}

with source_payments as (
    select 
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount
    from {{ source('dwh_raw', 'payments') }}
    {% if is_incremental() %}
        where payment_date::date > (select coalesce(max(payment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

order_lookup as (
    select 
        order_id,
        total_amount
    from {{ ref('fact_order') }}
),

payment_aggregation as (
    select 
        sp.order_id,
        sum(case when sp.amount >= 0 then sp.amount else 0 end) as total_payments
    from source_payments sp
    group by sp.order_id
),

transformed_payments as (
    select 
        -- Direct mapping for payment_id
        sp.payment_id,
        
        -- Lookup order_id and convert to integer
        ol.order_id::integer as order_id,
        
        -- Convert timestamp to date
        sp.payment_date::date as payment_date,
        
        -- Uppercase and map payment method to enum
        case 
            when upper(sp.payment_method) in ('CARD', 'UPI', 'COD', 'NETBANKING') 
            then upper(sp.payment_method)
            else 'CARD'
        end as payment_method,
        
        -- Handle negative amounts
        case 
            when sp.amount < 0 then null
            else sp.amount::number(10,2)
        end as amount,
        
        -- Calculate is_fully_applied flag
        case 
            when ol.total_amount is null then false
            when pa.total_payments >= ol.total_amount then true
            else false
        end as is_fully_applied
        
    from source_payments sp
    inner join order_lookup ol on sp.order_id = ol.order_id
    left join payment_aggregation pa on sp.order_id = pa.order_id
    -- Skip payments if order not present (inner join handles this)
)

select * from transformed_payments