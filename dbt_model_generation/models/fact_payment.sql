MODEL NAME: fact_payment
TARGET TABLE: bsl_ma.dwh_ma.fact_payment
SOURCE TABLES: bsl_raw.dwh_raw.payments, bsl_raw.dwh_raw.orders
DESCRIPTION: This model creates the fact_payment table by transforming payment data from raw sources, including payment validation, method standardization, and order relationship mapping.
PREREQUISITES: fact_order table must exist for order lookup and validation
PARAMETER: Incremental load based on payment_date
