{#
------------------------------------------------------------------------
MODEL NAME: fact_payment
TARGET TABLE: BSL_MA.DWH_MA.FACT_PAYMENT
SOURCE TABLES: BSL_RAW.DWH_RAW.PAYMENTS, BSL_RAW.DWH_RAW.ORDERS
DESCRIPTION: This model creates a fact table for payments by transforming raw payment data, validating payment methods, converting timestamps to dates, and determining if payments fully cover order amounts through order lookups.
PREREQUISITES: fact_order model should exist for order lookups
PARAMETER: Incremental model based on payment_date
AUTHOUR: AI Generated by Shajahan - 2024-12-19 IST
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='payment_id'
) }}

with source_payments as (
    select 
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount
    from {{ source('dwh_raw', 'payments') }}
    {% if is_incremental() %}
        where payment_date::date > (select coalesce(max(payment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

source_orders as (
    select 
        order_id,
        total_amount
    from {{ ref('fact_order') }}
),

payment_aggregates as (
    select 
        order_id,
        sum(case when amount >= 0 then amount else 0 end) as total_payments
    from source_payments
    where order_id is not null
    group by order_id
),

transformed_payments as (
    select 
        p.payment_id,
        p.order_id,
        p.payment_date::date as payment_date,
        case 
            when upper(p.payment_method) in ('CARD', 'UPI', 'COD', 'NETBANKING') 
            then upper(p.payment_method)
            else 'CARD'
        end as payment_method,
        case 
            when p.amount < 0 then null
            else p.amount::number(10,2)
        end as amount,
        case 
            when o.order_id is not null 
                and o.total_amount is not null 
                and pa.total_payments >= o.total_amount 
            then true
            else false
        end as is_fully_applied
    from source_payments p
    left join source_orders o on p.order_id = o.order_id
    left join payment_aggregates pa on p.order_id = pa.order_id
    where o.order_id is not null  -- Skip payment if order not present
)

select 
    payment_id,
    order_id,
    payment_date,
    payment_method,
    amount,
    is_fully_applied
from transformed_payments