{{ config(
    materialized = 'incremental',
    schema = 'dwh_ma',
    alias = 'fact_payment'
) }}

WITH source_payments AS (
    SELECT
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount
    FROM {{ source('dwh_raw', 'payments') }}
    {% if is_incremental() %}
        WHERE payment_date::date > (select coalesce(max(payment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

source_orders AS (
    SELECT
        order_id,
        total_amount
    FROM {{ source('dwh_raw', 'orders') }}
),

fact_order_ref AS (
    SELECT
        order_id
    FROM {{ ref('fact_order') }}
),

payment_aggregates AS (
    SELECT
        order_id,
        SUM(CASE WHEN amount >= 0 THEN amount ELSE 0 END) as total_payments
    FROM source_payments
    WHERE order_id IS NOT NULL
    GROUP BY order_id
),

transformed AS (
    SELECT
        p.payment_id,
        p.order_id,
        p.payment_date::date as payment_date,
        CASE 
            WHEN UPPER(p.payment_method) IN ('CARD', 'UPI', 'COD', 'NETBANKING') 
            THEN UPPER(p.payment_method)
            ELSE 'CARD'
        END as payment_method,
        CASE 
            WHEN p.amount < 0 THEN NULL
            ELSE p.amount::NUMBER(10,2)
        END as amount,
        CASE 
            WHEN o.total_amount IS NULL THEN FALSE
            WHEN pa.total_payments >= o.total_amount THEN TRUE
            ELSE FALSE
        END as is_fully_applied
    FROM source_payments p
    INNER JOIN fact_order_ref fo ON p.order_id = fo.order_id
    LEFT JOIN source_orders o ON p.order_id = o.order_id
    LEFT JOIN payment_aggregates pa ON p.order_id = pa.order_id
    WHERE p.order_id IS NOT NULL
)

SELECT * FROM transformed