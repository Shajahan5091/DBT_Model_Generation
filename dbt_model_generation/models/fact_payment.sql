{{ config(
    materialized = 'incremental',
    schema = 'dwh_ma',
    alias = 'fact_payment'
) }}

WITH source_payments AS (
    SELECT
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount
    FROM {{ source('dwh_raw', 'payments') }}
    {% if is_incremental() %}
    WHERE payment_date::date > (select coalesce(max(payment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

source_orders AS (
    SELECT
        order_id,
        total_amount
    FROM {{ source('dwh_raw', 'orders') }}
),

-- Validate orders exist in fact_order
valid_orders AS (
    SELECT DISTINCT order_id
    FROM {{ ref('fact_order') }}
),

-- Calculate payment totals per order
payment_totals AS (
    SELECT
        order_id,
        SUM(CASE WHEN amount >= 0 THEN amount ELSE 0 END) as total_payments
    FROM source_payments
    GROUP BY order_id
),

transformed AS (
    SELECT
        p.payment_id,
        p.order_id,
        p.payment_date::date as payment_date,
        CASE 
            WHEN UPPER(p.payment_method) IN ('CARD', 'UPI', 'COD', 'NETBANKING') 
            THEN UPPER(p.payment_method)
            ELSE 'CARD'
        END as payment_method,
        CASE 
            WHEN p.amount < 0 THEN NULL
            ELSE p.amount::NUMBER(10,2)
        END as amount,
        CASE 
            WHEN o.total_amount IS NULL THEN FALSE
            WHEN pt.total_payments >= o.total_amount THEN TRUE
            ELSE FALSE
        END as is_fully_applied
    FROM source_payments p
    INNER JOIN valid_orders vo ON p.order_id = vo.order_id
    LEFT JOIN source_orders o ON p.order_id = o.order_id
    LEFT JOIN payment_totals pt ON p.order_id = pt.order_id
)

SELECT * FROM transformed