WITH payments_source AS (
    SELECT
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount
    FROM {{ source('dwh_raw', 'payments') }}
    {% if is_incremental() %}
        WHERE payment_date::date > (select coalesce(max(payment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

orders_source AS (
    SELECT
        order_id,
        total_amount
    FROM {{ source('dwh_raw', 'orders') }}
),

-- Join payments with orders to validate order existence
payments_with_orders AS (
    SELECT
        p.payment_id,
        p.order_id,
        p.payment_date,
        p.payment_method,
        p.amount,
        o.total_amount as order_total_amount
    FROM payments_source p
    INNER JOIN orders_source o ON p.order_id = o.order_id
),

-- Calculate payment totals per order for is_fully_applied logic
payment_totals AS (
    SELECT
        order_id,
        SUM(CASE WHEN amount >= 0 THEN amount ELSE 0 END) as total_payments
    FROM payments_with_orders
    GROUP BY order_id
),

transformed AS (
    SELECT
        p.payment_id,
        p.order_id,
        p.payment_date::date as payment_date,
        CASE 
            WHEN UPPER(p.payment_method) IN ('CARD', 'UPI', 'COD', 'NETBANKING') 
            THEN UPPER(p.payment_method)
            ELSE 'CARD'
        END as payment_method,
        CASE 
            WHEN p.amount < 0 THEN NULL
            ELSE p.amount::NUMBER(10,2)
        END as amount,
        CASE 
            WHEN p.order_total_amount IS NULL THEN FALSE
            WHEN pt.total_payments >= p.order_total_amount THEN TRUE
            ELSE FALSE
        END as is_fully_applied
    FROM payments_with_orders p
    LEFT JOIN payment_totals pt ON p.order_id = pt.order_id
)

SELECT * FROM transformed