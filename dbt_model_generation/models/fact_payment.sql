MODEL NAME: fact_payment
TARGET TABLE: bsl_ma.dwh_ma.fact_payment
SOURCE TABLES: bsl_raw.dwh_raw.payments, bsl_ma.dwh_ma.fact_order
DESCRIPTION: This model transforms raw payment data into a fact table with proper data types, validation, and enrichment including order lookup and payment application status
PREREQUISITES: fact_order model must exist and be populated
PARAMETER: None
