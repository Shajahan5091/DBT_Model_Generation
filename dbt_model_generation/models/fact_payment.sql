{{ config(
    materialized = 'incremental',
    unique_key = 'payment_id'
) }}

WITH payments_source AS (
    SELECT
        p.payment_id,
        p.order_id,
        p.payment_date,
        p.payment_method,
        p.amount
    FROM {{ source('dwh_raw', 'payments') }} p
    {% if is_incremental() %}
        WHERE p.payment_date::date > (select coalesce(max(payment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

orders_source AS (
    SELECT
        o.order_id,
        o.total_amount
    FROM {{ source('dwh_raw', 'orders') }} o
),

fact_order_ref AS (
    SELECT
        fo.order_id,
        fo.total_amount
    FROM {{ ref('fact_order') }} fo
),

payment_order_joined AS (
    SELECT
        p.payment_id,
        p.order_id,
        p.payment_date,
        p.payment_method,
        p.amount,
        fo.total_amount AS order_total_amount
    FROM payments_source p
    INNER JOIN fact_order_ref fo ON p.order_id = fo.order_id
),

payment_aggregated AS (
    SELECT
        order_id,
        SUM(CASE WHEN amount >= 0 THEN amount ELSE 0 END) AS total_payments
    FROM payment_order_joined
    GROUP BY order_id
),

transformed AS (
    SELECT
        poj.payment_id,
        poj.order_id,
        poj.payment_date::date AS payment_date,
        CASE 
            WHEN UPPER(poj.payment_method) IN ('CARD', 'UPI', 'COD', 'NETBANKING') 
            THEN UPPER(poj.payment_method)
            ELSE 'CARD'
        END AS payment_method,
        CASE 
            WHEN poj.amount < 0 THEN NULL
            ELSE poj.amount
        END AS amount,
        CASE 
            WHEN poj.order_total_amount IS NULL THEN FALSE
            WHEN pa.total_payments >= poj.order_total_amount THEN TRUE
            ELSE FALSE
        END AS is_fully_applied
    FROM payment_order_joined poj
    LEFT JOIN payment_aggregated pa ON poj.order_id = pa.order_id
)

SELECT * FROM transformed