{{ config(
    materialized='incremental',
    unique_key='order_id'
) }}

with source_orders as (
    select 
        order_id,
        customer_id,
        order_date,
        status,
        total_amount
    from {{ source('dwh_raw', 'orders') }}
    {% if is_incremental() %}
        where order_date::date > (select coalesce(max(order_date), '1900-01-01') from {{ this }})
    {% endif %}
),

customer_lookup as (
    select 
        customer_id
    from {{ ref('dim_customer') }}
),

transformed_orders as (
    select 
        so.order_id,
        cl.customer_id::integer as customer_id,
        so.order_date::date as order_date,
        case 
            when upper(so.status) in ('PENDING', 'PAID', 'CANCELLED', 'SHIPPED') 
            then upper(so.status)
            else 'PENDING'
        end as order_status,
        case 
            when so.total_amount < 0 then null
            else so.total_amount::number(10,2)
        end as total_amount
    from source_orders so
    inner join customer_lookup cl
        on so.customer_id = cl.customer_id
)

select 
    order_id,
    customer_id,
    order_date,
    order_status,
    total_amount
from transformed_orders