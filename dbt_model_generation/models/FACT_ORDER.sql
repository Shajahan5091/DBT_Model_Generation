SOURCE TABLES: BSL_RAW.DWH_RAW.ORDERS
DESCRIPTION: Order fact table at order level that transforms raw order data into a structured fact table with surrogate keys and data quality validations
PREREQUISITES: DIM_CUSTOMER table must exist for customer lookup
PARAMETER: Incremental load based on ORDER_DATE
