{{ config(materialized='table') }}

with source_data as (
    select 
        product_id,
        supplier_id,
        stock_qty,
        warehouse_location,
        last_updated
    from {{ source('dwh_raw', 'inventory') }}
),

-- Add row numbers to identify latest records per composite key
ranked_data as (
    select 
        *,
        row_number() over (
            partition by product_id, supplier_id 
            order by last_updated desc
        ) as rn
    from source_data
),

-- Transform and clean the data
transformed_data as (
    select 
        -- Product ID lookup with unknown handling
        coalesce(dp.product_id, 0) as product_id,
        
        -- Supplier ID lookup with unknown handling  
        coalesce(ds.supplier_id, 0) as supplier_id,
        
        -- Stock quantity with negative value handling
        case 
            when sd.stock_qty < 0 then null
            else sd.stock_qty
        end as stock_qty,
        
        -- Warehouse location uppercase transformation
        upper(sd.warehouse_location) as warehouse_location,
        
        -- Convert timestamp to date
        sd.last_updated::date as snapshot_date,
        
        -- Flag latest record per composite key
        case when sd.rn = 1 then true else false end as is_latest,
        
        -- Retain original values for audit
        sd.product_id as original_product_id,
        sd.supplier_id as original_supplier_id,
        sd.last_updated
        
    from ranked_data sd
    left join {{ ref('dim_product') }} dp 
        on sd.product_id = dp.product_id
    left join {{ ref('dim_supplier') }} ds 
        on sd.supplier_id = ds.supplier_id
)

select 
    product_id,
    supplier_id,
    stock_qty,
    warehouse_location,
    snapshot_date,
    is_latest,
    original_product_id,
    original_supplier_id,
    last_updated
from transformed_data