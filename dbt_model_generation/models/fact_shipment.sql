{{ config(
    materialized='incremental',
    unique_key='shipment_id'
) }}

with source_shipments as (
    select 
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number
    from {{ source('dwh_raw', 'shipments') }}
    {% if is_incremental() %}
        where shipped_date::date > (select coalesce(max(shipped_date), '1900-01-01') from {{ this }})
    {% endif %}
),

fact_order_lookup as (
    select 
        order_id,
        order_date
    from {{ ref('fact_order') }}
),

shipments_with_order_data as (
    select 
        s.shipment_id,
        s.order_id,
        s.shipped_date::date as shipped_date,
        upper(s.carrier) as carrier,
        s.tracking_number,
        o.order_date
    from source_shipments s
    inner join fact_order_lookup o
        on s.order_id = o.order_id
),

final as (
    select 
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number,
        greatest(0, datediff('day', order_date::date, shipped_date)) as days_to_ship
    from shipments_with_order_data
)

select * from final