{#
------------------------------------------------------------------------
MODEL NAME: fact_shipment
TARGET TABLE: bsl_ma.dwh_ma.fact_shipment
SOURCE TABLES: bsl_raw.dwh_raw.shipments
DESCRIPTION: This model transforms raw shipment data into a fact table for analytics, including shipment details, carrier information, and calculated days to ship metrics.
PREREQUISITES: fact_order table must exist and be populated with order data
PARAMETER: None
AUTHOUR: AI Generated by Shajahan - 2024-12-19
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='shipment_id'
) }}

with source_data as (
    select 
        shipment_id,
        order_id,
        shipment_date,
        order_date,
        carrier,
        tracking_number
    from {{ source('dwh_raw', 'shipments') }}
    {% if is_incremental() %}
        where shipment_date::date > (select coalesce(max(shipment_date), '1900-01-01') from {{ this }})
    {% endif %}
),

fact_order_lookup as (
    select 
        order_id,
        order_date
    from {{ ref('fact_order') }}
),

transformed_data as (
    select 
        s.shipment_id,
        s.order_id,
        s.shipment_date::date as shipment_date,
        upper(s.carrier) as carrier,
        s.tracking_number,
        greatest(0, datediff('day', coalesce(f.order_date, s.order_date), s.shipment_date)) as days_to_ship
    from source_data s
    left join fact_order_lookup f
        on s.order_id = f.order_id
    where s.order_id is not null
        and f.order_id is not null
)

select * from transformed_data