SOURCE TABLES: bsl_raw.dwh_raw.shipments, bsl_raw.dwh_raw.orders
DESCRIPTION: Fact table for shipment data with order lookup and calculated days to ship
PREREQUISITES: fact_order model must exist for order_id lookup
