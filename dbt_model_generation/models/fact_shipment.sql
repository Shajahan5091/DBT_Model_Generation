WITH shipments_source AS (
    SELECT
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number
    FROM {{ source('dwh_raw', 'shipments') }}
    {% if is_incremental() %}
        WHERE shipped_date::date > (select coalesce(max(shipped_date), '1900-01-01') from {{ this }})
    {% endif %}
),

orders_source AS (
    SELECT
        order_id,
        order_date
    FROM {{ source('dwh_raw', 'orders') }}
),

shipments_with_orders AS (
    SELECT
        shipments.shipment_id,
        shipments.order_id,
        shipments.shipped_date::date as shipped_date,
        UPPER(shipments.carrier) as carrier,
        shipments.tracking_number,
        orders.order_date
    FROM shipments_source as shipments
    INNER JOIN orders_source as orders
        ON shipments.order_id = orders.order_id
),

final_transformations AS (
    SELECT
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number,
        GREATEST(0, DATEDIFF('day', order_date, shipped_date)) as days_to_ship
    FROM shipments_with_orders
)

SELECT * FROM final_transformations