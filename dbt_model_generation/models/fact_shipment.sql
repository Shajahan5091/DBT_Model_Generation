WITH source_shipments AS (
    SELECT
        s.shipment_id,
        s.order_id,
        s.shipped_date,
        s.carrier,
        s.tracking_number
    FROM {{ source('dwh_raw', 'shipments') }} s
    {{ incremental_filter }}
),

source_orders AS (
    SELECT
        o.order_id,
        o.order_date
    FROM {{ source('dwh_raw', 'orders') }} o
),

joined_data AS (
    SELECT
        s.shipment_id,
        s.order_id,
        s.shipped_date,
        s.carrier,
        s.tracking_number,
        o.order_date
    FROM source_shipments s
    INNER JOIN source_orders o
        ON s.order_id = o.order_id
),

transformed AS (
    SELECT
        t.shipment_id AS fs_shipment_id,
        t.order_id AS fs_order_id,
        t.shipped_date::date AS fs_shipped_date,
        UPPER(t.carrier) AS fs_carrier,
        t.tracking_number AS fs_tracking_number,
        GREATEST(0, DATEDIFF('day', t.order_date::date, t.shipped_date::date)) AS fs_days_to_ship
    FROM joined_data t
)

