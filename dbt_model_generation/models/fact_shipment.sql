{{ config(
    materialized = 'incremental',
    alias = 'fact_shipment'
) }}

WITH source_shipments AS (
    SELECT
        s.shipment_id,
        s.order_id,
        s.shipped_date,
        s.carrier,
        s.tracking_number
    FROM {{ source('dwh_raw', 'shipments') }} s
    {% if is_incremental() %}
        WHERE s.shipped_date::date > (select coalesce(max(shipped_date), '1900-01-01') from {{ this }})
    {% endif %}
),

source_orders AS (
    SELECT
        o.order_id,
        o.order_date
    FROM {{ source('dwh_raw', 'orders') }} o
),

order_lookup AS (
    SELECT
        fo.order_id
    FROM {{ ref('fact_order') }} fo
),

transformed AS (
    SELECT
        s.shipment_id,
        s.order_id,
        s.shipped_date::date as shipped_date,
        GREATEST(0, DATEDIFF('day', o.order_date::date, s.shipped_date::date)) as days_to_ship,
        UPPER(s.carrier) as carrier,
        s.tracking_number
    FROM source_shipments s
    INNER JOIN source_orders o ON s.order_id = o.order_id
    INNER JOIN order_lookup ol ON s.order_id = ol.order_id
)

SELECT * FROM transformed