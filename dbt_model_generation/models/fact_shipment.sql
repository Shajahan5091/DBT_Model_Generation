{{ config(
    materialized='incremental',
    unique_key='shipment_id'
) }}

with shipments_source as (
    select 
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number
    from {{ source('dwh_raw', 'shipments') }}
    {% if is_incremental() %}
        where shipped_date::date > (select coalesce(max(shipped_date), '1900-01-01') from {{ this }})
    {% endif %}
),

orders_source as (
    select 
        order_id,
        order_date
    from {{ source('dwh_raw', 'orders') }}
),

shipments_with_orders as (
    select 
        s.shipment_id,
        s.order_id,
        s.shipped_date::date as shipped_date,
        upper(s.carrier) as carrier,
        s.tracking_number,
        o.order_date,
        greatest(0, datediff('day', o.order_date::date, s.shipped_date::date)) as days_to_ship
    from shipments_source s
    inner join orders_source o 
        on s.order_id = o.order_id
)

select 
    shipment_id,
    order_id,
    shipped_date,
    carrier,
    tracking_number,
    days_to_ship
from shipments_with_orders