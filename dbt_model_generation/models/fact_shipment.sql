{{ config(
    materialized = 'incremental',
    schema = 'dwh_ma',
    alias = 'fact_shipment'
) }}

WITH shipments_source AS (
    SELECT
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number
    FROM {{ source('dwh_raw', 'shipments') }}
    {% if is_incremental() %}
    WHERE shipped_date::date > (select coalesce(max(shipped_date), '1900-01-01') from {{ this }})
    {% endif %}
),

orders_source AS (
    SELECT
        order_id,
        order_date
    FROM {{ source('dwh_raw', 'orders') }}
),

-- Join shipments with orders to get order_date for days_to_ship calculation
shipments_with_orders AS (
    SELECT
        s.shipment_id,
        s.order_id,
        s.shipped_date,
        s.carrier,
        s.tracking_number,
        o.order_date
    FROM shipments_source s
    INNER JOIN orders_source o ON s.order_id = o.order_id
    -- Skip records where order is missing as per notes
),

transformed AS (
    SELECT
        shipment_id,
        order_id,
        shipped_date::date as shipped_date,
        -- Calculate days to ship with floor at 0 for negative values
        GREATEST(0, DATEDIFF('day', order_date::date, shipped_date::date)) as days_to_ship,
        UPPER(carrier) as carrier,
        tracking_number
    FROM shipments_with_orders
)

SELECT * FROM transformed