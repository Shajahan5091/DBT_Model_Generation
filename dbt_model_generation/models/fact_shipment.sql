{#
------------------------------------------------------------------------
MODEL NAME: fact_shipment
TARGET TABLE: bsl_ma.dwh_ma.fact_shipment
SOURCE TABLES: bsl_raw.dwh_raw.shipments
DESCRIPTION: This model transforms raw shipment data into a fact table for analytics, including shipment details, carrier information, and calculated days to ship metrics.
PREREQUISITES: fact_order table must exist for order lookup and date calculations
PARAMETER: None
AUTHOUR: AI Generated by Shajahan - 2024-12-19
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='shipment_id'
) }}

with source_shipments as (
    select 
        shipment_id,
        order_id,
        shipped_date,
        order_date,
        carrier,
        tracking_number
    from {{ source('dwh_raw', 'shipments') }}
    {% if is_incremental() %}
        where shipped_date::date > (select coalesce(max(shipped_date), '1900-01-01') from {{ this }})
    {% endif %}
),

fact_order_lookup as (
    select 
        order_id,
        order_date
    from {{ ref('fact_order') }}
),

transformed_shipments as (
    select 
        s.shipment_id,
        s.order_id,
        s.shipped_date::date as shipped_date,
        greatest(0, datediff('day', coalesce(f.order_date, s.order_date), s.shipped_date)) as days_to_ship,
        upper(s.carrier) as carrier,
        s.tracking_number
    from source_shipments s
    left join fact_order_lookup f 
        on s.order_id = f.order_id
    where s.order_id is not null 
        and f.order_id is not null
)

select * from transformed_shipments