WITH shipments_source AS (
    SELECT
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number
    FROM {{ source('dwh_raw', 'shipments') }}
    {% if is_incremental() %}
        WHERE shipped_date::date > (select coalesce(max(shipped_date), '1900-01-01') from {{ this }})
    {% endif %}
),

orders_source AS (
    SELECT
        order_id,
        order_date
    FROM {{ source('dwh_raw', 'orders') }}
),

transformed AS (
    SELECT
        s.shipment_id,
        s.order_id,
        s.shipped_date::date as shipped_date,
        UPPER(s.carrier) as carrier,
        s.tracking_number,
        GREATEST(0, DATEDIFF('day', o.order_date::date, s.shipped_date::date)) as days_to_ship
    FROM shipments_source s
    LEFT JOIN orders_source o
        ON s.order_id = o.order_id
    WHERE o.order_id IS NOT NULL  -- Skip if order missing
)

SELECT * FROM transformed