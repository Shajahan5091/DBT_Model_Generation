{{ config(
    materialized = 'incremental',
    schema = 'dwh_ma',
    alias = 'fact_order',
    unique_key = 'order_id'
) }}

WITH source_data AS (
    SELECT
        order_id,
        customer_id,
        order_date,
        status,
        total_amount
    FROM {{ source('dwh_raw', 'orders') }}
    {% if is_incremental() %}
        WHERE order_date::date > (select coalesce(max(order_date), '1900-01-01') from {{ this }})
    {% endif %}
),

customer_lookup AS (
    SELECT DISTINCT customer_id
    FROM {{ ref('dim_customer') }}
),

transformed AS (
    SELECT
        src.order_id,
        src.customer_id,
        src.order_date::date as order_date,
        CASE 
            WHEN UPPER(src.status) IN ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            THEN UPPER(src.status)
            ELSE 'PENDING'
        END as order_status,
        CASE 
            WHEN src.total_amount < 0 THEN NULL
            ELSE src.total_amount::NUMBER(10,2)
        END as total_amount
    FROM source_data src
    INNER JOIN customer_lookup cl ON src.customer_id = cl.customer_id
)

SELECT * FROM transformed