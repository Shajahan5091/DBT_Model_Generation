{{ config(
    materialized = 'incremental',
    unique_key = 'order_id',
    incremental_strategy = 'merge'
) }}

WITH source_data AS (
    SELECT
        order_id,
        customer_id,
        order_date,
        status,
        total_amount
    FROM {{ source('dwh_raw', 'orders') }}
    {% if is_incremental() %}
        WHERE order_date::date > (select coalesce(max(order_date), '1900-01-01') from {{ this }})
    {% endif %}
),

customer_lookup AS (
    SELECT
        customer_id
    FROM {{ ref('dim_customer') }}
),

transformed AS (
    SELECT
        s.order_id,
        s.customer_id,
        s.order_date::date as order_date,
        CASE 
            WHEN UPPER(s.status) IN ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            THEN UPPER(s.status)
            ELSE 'PENDING'
        END as order_status,
        CASE 
            WHEN s.total_amount < 0 THEN NULL
            ELSE s.total_amount::NUMBER(10,2)
        END as total_amount
    FROM source_data s
    INNER JOIN customer_lookup c
        ON s.customer_id = c.customer_id
)

SELECT * FROM transformed