{{ config(
    materialized = 'incremental',
    unique_key = 'order_id',
    incremental_strategy = 'merge'
) }}

WITH source_data AS (
    SELECT
        o.order_id,
        o.customer_id,
        o.order_date,
        o.status,
        o.total_amount
    FROM {{ source('dwh_raw', 'orders') }} o
    {% if is_incremental() %}
        WHERE o.order_date::date > (select coalesce(max(order_date), '1900-01-01') from {{ this }})
    {% endif %}
),

customer_lookup AS (
    SELECT
        c.customer_id
    FROM {{ ref('dim_customer') }} c
),

transformed AS (
    SELECT
        s.order_id,
        s.customer_id,
        s.order_date::date as order_date,
        CASE 
            WHEN UPPER(s.status) IN ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            THEN UPPER(s.status)
            ELSE 'PENDING'
        END as order_status,
        CASE 
            WHEN s.total_amount < 0 THEN NULL
            ELSE s.total_amount
        END as total_amount
    FROM source_data s
    INNER JOIN customer_lookup c
        ON s.customer_id = c.customer_id
)

SELECT * FROM transformed