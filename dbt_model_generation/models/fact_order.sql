MODEL NAME: fact_order
TARGET TABLE: bsl_ma.dwh_ma.fact_order
SOURCE TABLES: bsl_raw.dwh_raw.orders
DESCRIPTION: This model transforms raw order data into a fact table for analytical purposes, including customer lookups, status normalization, and data type conversions.
PREREQUISITES: dim_customer table must exist for customer lookup operations
PARAMETER: Incremental load based on order_date
AUTHOUR: AI Generated by Shajahan - 2024-12-19
------------------------------------------------------------------------
#}

{{ config(
    materialized = 'incremental',
    unique_key = 'order_id'
) }}

WITH source_data AS (
    SELECT
        o.order_id,
        o.customer_id,
        o.order_date,
        o.status,
        o.total_amount
    FROM {{ source('dwh_raw', 'orders') }} o
    {% if is_incremental() %}
        WHERE o.order_date::date > (select coalesce(max(order_date), '1900-01-01') from {{ this }})
    {% endif %}
),

customer_lookup AS (
    SELECT
        c.customer_id
    FROM {{ ref('dim_customer') }} c
),

transformed AS (
    SELECT
        s.order_id,
        s.customer_id,
        s.order_date::date as order_date,
        CASE 
            WHEN UPPER(s.status) IN ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            THEN UPPER(s.status)
            ELSE 'PENDING'
        END as order_status,
        CASE 
            WHEN s.total_amount < 0 THEN NULL
            ELSE s.total_amount
        END as total_amount
    FROM source_data s
    INNER JOIN customer_lookup c ON s.customer_id = c.customer_id
)

SELECT * FROM transformed