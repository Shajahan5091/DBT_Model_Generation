{{ config(
    materialized = 'incremental',
    schema = 'dwh_ma',
    alias = 'fact_order',
    unique_key = 'order_id'
) }}

WITH source_data AS (
    SELECT
        order_id,
        customer_id,
        order_date,
        status,
        total_amount
    FROM {{ source('dwh_raw', 'orders') }}
    {% if is_incremental() %}
        WHERE order_date::date > (select coalesce(max(order_date), '1900-01-01') from {{ this }})
    {% endif %}
),

customer_lookup AS (
    SELECT
        customer_id
    FROM {{ ref('dim_customer') }}
),

transformed AS (
    SELECT
        sd.order_id,
        sd.customer_id,
        sd.order_date::date as order_date,
        CASE 
            WHEN UPPER(sd.status) IN ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            THEN UPPER(sd.status)
            ELSE 'PENDING'
        END as order_status,
        CASE 
            WHEN sd.total_amount < 0 THEN NULL
            ELSE sd.total_amount::NUMBER(10,2)
        END as total_amount
    FROM source_data sd
    INNER JOIN customer_lookup cl ON sd.customer_id = cl.customer_id
)

SELECT * FROM transformed