WITH source_data AS (
    SELECT
        category_id,
        category_name,
        parent_category_id,
        is_active
    FROM {{ source('dwh_raw', 'categories') }}
),

-- Get parent category names for building hierarchical path
parent_categories AS (
    SELECT
        category_id,
        category_name AS parent_category_name
    FROM {{ source('dwh_raw', 'categories') }}
),

transformed AS (
    SELECT
        -- Direct mapping for category_id
        s.category_id,
        
        -- Trim and title-case transformation for category_name
        INITCAP(TRIM(s.category_name)) AS category_name,
        
        -- Direct mapping for parent_category_id (can be null for root categories)
        s.parent_category_id,
        
        -- Build category path up to 2 levels: PARENT_category_name > category_name
        CASE 
            WHEN s.parent_category_id IS NOT NULL AND p.parent_category_name IS NOT NULL
            THEN CONCAT(INITCAP(TRIM(p.parent_category_name)), ' > ', INITCAP(TRIM(s.category_name)))
            ELSE INITCAP(TRIM(s.category_name))
        END AS category_path,
        
        -- Direct mapping for is_active with default true if null
        COALESCE(s.is_active, TRUE) AS is_active
        
    FROM source_data s
    LEFT JOIN parent_categories p 
        ON s.parent_category_id = p.category_id
)

SELECT * FROM transformed