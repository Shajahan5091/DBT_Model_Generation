WITH source_data AS (
    SELECT
        src.category_id,
        src.category_name,
        src.parent_category_id,
        src.is_active
    FROM {{ source('dwh_raw', 'categories') }} src
),

parent_categories AS (
    SELECT
        p.category_id as parent_id,
        p.category_name as parent_name
    FROM {{ source('dwh_raw', 'categories') }} p
),

transformed AS (
    SELECT
        src.category_id,
        TRIM(INITCAP(src.category_name)) as category_name,
        src.parent_category_id,
        CASE 
            WHEN src.parent_category_id IS NOT NULL AND p.parent_name IS NOT NULL 
            THEN TRIM(INITCAP(p.parent_name)) || ' > ' || TRIM(INITCAP(src.category_name))
            ELSE TRIM(INITCAP(src.category_name))
        END as category_path,
        COALESCE(src.is_active, TRUE) as is_active
    FROM source_data src
    LEFT JOIN parent_categories p ON src.parent_category_id = p.parent_id
)

SELECT * FROM transformed