WITH source_data AS (
    SELECT
        src.category_id,
        src.category_name,
        src.parent_category_id,
        src.is_active
    FROM {{ source('dwh_raw', 'categories') }} src
),

parent_categories AS (
    SELECT
        src.category_id,
        src.category_name,
        src.parent_category_id,
        src.is_active
    FROM {{ source('dwh_raw', 'categories') }} src
),

transformed AS (
    SELECT
        src.category_id,
        INITCAP(TRIM(src.category_name)) AS category_name,
        src.parent_category_id,
        CASE 
            WHEN parent.category_name IS NOT NULL THEN 
                INITCAP(TRIM(parent.category_name)) || ' > ' || INITCAP(TRIM(src.category_name))
            ELSE 
                INITCAP(TRIM(src.category_name))
        END AS category_path,
        COALESCE(src.is_active, TRUE) AS is_active
    FROM source_data src
    LEFT JOIN parent_categories parent
        ON src.parent_category_id = parent.category_id
)

SELECT * FROM transformed