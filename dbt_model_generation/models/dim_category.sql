WITH source_data AS (
    SELECT
        category_id,
        category_name,
        parent_category_id,
        is_active
    FROM {{ source('dwh_raw', 'categories') }}
),

parent_categories AS (
    SELECT
        category_id,
        TRIM(INITCAP(category_name)) AS parent_category_name
    FROM {{ source('dwh_raw', 'categories') }}
),

transformed AS (
    SELECT
        s.category_id,
        TRIM(INITCAP(s.category_name)) AS category_name,
        s.parent_category_id,
        CASE 
            WHEN s.parent_category_id IS NOT NULL 
            THEN CONCAT(p.parent_category_name, ' > ', TRIM(INITCAP(s.category_name)))
            ELSE TRIM(INITCAP(s.category_name))
        END AS category_path,
        COALESCE(s.is_active, TRUE) AS is_active
    FROM source_data s
    LEFT JOIN parent_categories p 
        ON s.parent_category_id = p.category_id
)

SELECT * FROM transformed