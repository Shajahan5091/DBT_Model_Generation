with source_categories as (
    select
        category_id,
        category_name,
        parent_category_id,
        is_active
    from {{ source('dwh_raw', 'categories') }}
),

parent_categories as (
    select
        category_id as parent_id,
        category_name as parent_name
    from {{ source('dwh_raw', 'categories') }}
),

transformed_categories as (
    select
        -- Direct mapping for category_id with unique constraint
        sc.category_id,
        
        -- Trim and title-case transformation for category_name
        initcap(trim(sc.category_name)) as category_name,
        
        -- Direct mapping for parent_category_id (may be null for root categories)
        sc.parent_category_id,
        
        -- Build category path up to 2 levels: PARENT_category_name > category_name
        case 
            when sc.parent_category_id is not null and pc.parent_name is not null then
                initcap(trim(pc.parent_name)) || ' > ' || initcap(trim(sc.category_name))
            else
                initcap(trim(sc.category_name))
        end as category_path,
        
        -- Direct mapping for is_active with default true if null
        coalesce(sc.is_active, true) as is_active
        
    from source_categories sc
    left join parent_categories pc
        on sc.parent_category_id = pc.parent_id
)

select
    category_id,
    category_name,
    parent_category_id,
    category_path,
    is_active
from transformed_categories