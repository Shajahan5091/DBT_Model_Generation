WITH source_data AS (
    SELECT
        category_id,
        category_name,
        parent_category_id,
        is_active
    FROM {{ source('dwh_raw', 'categories') }}
),

parent_categories AS (
    SELECT
        category_id as parent_id,
        category_name as parent_name
    FROM {{ source('dwh_raw', 'categories') }}
),

transformed AS (
    SELECT
        s.category_id as category_id,
        TRIM(INITCAP(s.category_name)) as category_name,
        s.parent_category_id as parent_category_id,
        CASE 
            WHEN s.parent_category_id IS NOT NULL 
            THEN TRIM(INITCAP(p.parent_name)) || ' > ' || TRIM(INITCAP(s.category_name))
            ELSE TRIM(INITCAP(s.category_name))
        END as category_path,
        COALESCE(s.is_active, TRUE) as is_active
    FROM source_data s
    LEFT JOIN parent_categories p ON s.parent_category_id = p.parent_id
)

SELECT * FROM transformed