with source_data as (
    select 
        category_id,
        category_name,
        parent_category_id,
        is_active
    from {{ source('dwh_raw', 'categories') }}
),

parent_categories as (
    select 
        category_id,
        category_name as parent_category_name
    from {{ source('dwh_raw', 'categories') }}
),

transformed_data as (
    select 
        -- Direct mapping with unique test
        s.category_id,
        
        -- Trim and title-case transformation
        initcap(trim(s.category_name)) as category_name,
        
        -- Direct mapping - parent may be null for root categories
        s.parent_category_id,
        
        -- Build category path up to 2 levels: PARENT_category_name > category_name
        case 
            when s.parent_category_id is not null and p.parent_category_name is not null then
                initcap(trim(p.parent_category_name)) || ' > ' || initcap(trim(s.category_name))
            else 
                initcap(trim(s.category_name))
        end as category_path,
        
        -- Direct mapping with default true for null values
        coalesce(s.is_active, true) as is_active
        
    from source_data s
    left join parent_categories p 
        on s.parent_category_id = p.category_id
)

select * from transformed_data