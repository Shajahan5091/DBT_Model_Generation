{{ config(materialized='table') }}

with source_data as (
    select
        supplier_id,
        supplier_name,
        contact_email,
        country,
        active_flag
    from {{ source('dwh_raw', 'suppliers') }}
),

transformed_data as (
    select
        supplier_id,
        trim(initcap(supplier_name)) as supplier_name,
        case 
            when contact_email is not null 
                and contact_email rlike '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$'
            then lower(contact_email)
            else null
        end as contact_email,
        upper(country) as country,
        coalesce(active_flag, false) as is_active
    from source_data
)

select * from transformed_data