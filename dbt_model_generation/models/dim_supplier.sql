    materialized = 'table',
    alias = 'dim_supplier'
) }}

WITH source_data AS (
    SELECT
        supplier_id,
        supplier_name,
        contact_email,
        country,
        active_flag
    FROM {{ source('dwh_raw', 'suppliers') }}
),

transformed AS (
    SELECT
        supplier_id,
        TRIM(INITCAP(supplier_name)) AS supplier_name,
        CASE 
            WHEN REGEXP_LIKE(LOWER(contact_email), '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$') 
            THEN LOWER(contact_email)
            ELSE NULL 
        END AS contact_email,
        UPPER(country) AS country,
        COALESCE(active_flag, FALSE) AS is_active
    FROM source_data
)

SELECT * FROM transformed;