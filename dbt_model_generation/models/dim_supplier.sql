WITH source_data AS (
    SELECT
        supplier_id,
        supplier_name,
        contact_email,
        country,
        active_flag
    FROM {{ source('dwh_raw', 'suppliers') }}
),

transformed AS (
    SELECT
        supplier_id AS sup_supplier_id,
        TRIM(INITCAP(supplier_name)) AS sup_supplier_name,
        CASE 
            WHEN REGEXP_LIKE(LOWER(contact_email), '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$')
            THEN LOWER(contact_email)
            ELSE NULL
        END AS sup_contact_email,
        UPPER(country) AS sup_country,
        COALESCE(active_flag, FALSE) AS sup_is_active
    FROM source_data
)

SELECT * FROM transformed;